/*
 * 구문분석기.h - 글 언어 파서
 *
 * 재귀 하강 파서로 토큰 스트림을 AST로 변환한다.
 * 한글 조사 기반 인자 바인딩, 어미 기반 제어흐름을 처리한다.
 */
#ifndef GEUL_PARSER_H
#define GEUL_PARSER_H

#include "lexer.h"
#include "ast.h"

typedef struct {
    형태소분석기 *분석기;

    토큰 현재;           /* 현재 토큰 */
    토큰 이전;           /* 직전에 소비된 토큰 */

    /* 오류 처리 */
    char 오류목록[64][256];
    int 오류_수;
    bool 패닉_모드;      /* 오류 복구 중 */

    /* 정의된 묶음(struct) 이름 등록 */
    char *묶음이름목록[128];
    int 묶음이름_수;

    /* 정의된 함수 이름 등록 — 동사어근→명사형 폴백용 */
    char *함수이름목록[512];
    int 함수이름_수;
} 구문분석기;

/* 파서 초기화 */
void 파서_초기화(구문분석기 *파서, 형태소분석기 *분석기);

/* 프로그램 전체를 파싱하여 AST 루트를 반환 */
노드 *프로그램_파싱(구문분석기 *파서);

/* 오류 존재 여부 */
bool 파서_오류있는가(구문분석기 *파서);

/* 오류 메시지 출력 */
void 파서_오류출력(구문분석기 *파서);

#endif /* GEUL_PARSER_H */
