/*
 * 형태소분석기.h - 글 언어의 어휘 분석기 (Lexer)
 *
 * 일반 프로그래밍 언어의 렉서와 달리, 한글 단어를
 * 어근+조사/어미로 분리하는 형태소 분석 기능을 포함한다.
 *
 * "사과가격을" → "사과가격"(식별자) + "을"(조사:목적어)
 * "출력하다"   → "출력"(동사어근) + "하"(동사화) + "다"(어미:종결)
 */
#ifndef GEUL_LEXER_H
#define GEUL_LEXER_H

#include "token.h"
#include "unicode.h"

/* 최대 토큰 텍스트 길이 (바이트) */
#define 최대_토큰_길이 1024

/* ===== 렉서 상태 ===== */

typedef struct {
    /* 소스 */
    const char *소스;           /* 전체 소스 텍스트 (UTF-8) */
    const char *현재;           /* 현재 읽기 위치 */
    const char *소스끝;         /* 소스 텍스트 끝 */

    /* 위치 추적 */
    const char *파일명;
    int 줄;
    int 열;

    /* 토큰 버퍼 (형태소 분리 시 여러 토큰이 생길 수 있음) */
    토큰 버퍼[8];              /* 분리된 토큰 대기열 */
    int 버퍼_수;               /* 대기 중인 토큰 수 */
    int 버퍼_위치;             /* 현재 읽기 위치 */

    /* 오류 */
    char 오류_메시지[512];
    bool 오류_발생;
} 형태소분석기;

/* ===== 렉서 생성/소멸 ===== */

/*
 * 형태소 분석기를 초기화한다.
 * 소스: UTF-8로 인코딩된 소스 텍스트 (NFC 정규화 완료 상태여야 함)
 * 길이: 소스 텍스트 길이 (바이트)
 * 파일명: 오류 보고용 파일명
 */
void 분석기_초기화(형태소분석기 *분석기, const char *소스, size_t 길이, const char *파일명);

/* ===== 토큰 읽기 ===== */

/*
 * 다음 토큰을 읽어 반환한다.
 * 파일 끝이면 토큰_파일끝을 반환한다.
 */
토큰 다음_토큰(형태소분석기 *분석기);

/*
 * 다음 토큰을 미리 본다. (소비하지 않음)
 */
토큰 미리보기(형태소분석기 *분석기);

#endif /* GEUL_LEXER_H */
