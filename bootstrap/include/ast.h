/*
 * 구문트리.h - 추상 구문 트리 (AST) 정의
 *
 * 글 언어의 모든 구문 요소를 트리 구조로 표현한다.
 * 조사 역할 정보와 어미 기능 정보가 노드에 포함된다.
 */
#ifndef GEUL_AST_H
#define GEUL_AST_H

#include "token.h"
#include <stdbool.h>
#include <stddef.h>

/* ===== 노드 종류 ===== */

typedef enum {
    /* 최상위 */
    노드_프로그램,

    /* 선언 */
    노드_함수정의,
    노드_변수선언,
    노드_묶음정의,       /* struct */
    노드_나열정의,       /* enum */
    노드_별칭정의,       /* typedef */
    노드_합침정의,       /* union */

    /* 문장 */
    노드_블록,
    노드_조건문,         /* -면 (if) */
    노드_동안반복,       /* -동안 (while) */
    노드_범위반복,       /* 부터~까지 (for range) */
    노드_반복문,         /* 반복 (C-style for loop) */
    노드_반복하기,       /* do-while */
    노드_이동문,         /* goto */
    노드_레이블,         /* label: */
    노드_전처리_만약정의,  /* #ifdef */
    노드_전처리_아니면,    /* #else */
    노드_전처리_끝,        /* #endif */
    노드_전처리_만약,      /* #if */
    노드_매크로정의,       /* 정의 이름 값. (#define) */
    노드_반환문,
    노드_탈출문,         /* break */
    노드_계속문,         /* continue */
    노드_표현식문,       /* 표현식으로 구성된 문장 */
    노드_갈래문,         /* switch */

    /* 표현식 */
    노드_정수_리터럴,
    노드_실수_리터럴,
    노드_문자열_리터럴,
    노드_문자_리터럴,
    노드_참거짓_리터럴,
    노드_없음_리터럴,
    노드_식별자,
    노드_이항연산,       /* 가 + 나 */
    노드_단항연산,       /* !가, -가 */
    노드_호출,           /* 조사 기반 함수 호출 */
    노드_멤버접근,       /* 의 (.) */
    노드_배열접근,       /* [인덱스] */
    노드_대입,           /* 에 넣다 */
    노드_형변환,         /* 로/으로 (타입 변환) */
    노드_주소참조,       /* &참조 */
    노드_역참조,         /* *참조 */
    노드_크기,           /* 크기(타입) */
    노드_파이프라인,     /* -어서/-아서 체이닝 */
    노드_묶음_리터럴,   /* 구조체 초기화 { 필드: 값, ... } */
    노드_배열_리터럴,   /* 배열 초기화 { 값1, 값2, ... } */
    노드_증감,           /* ++/-- (전위/후위) */
    노드_삼항,           /* 조건 ? 참값 : 거짓값 */
    노드_가변시작,       /* va_start(ap, param) */
    노드_가변끝,         /* va_end(ap) */
    노드_가변인자,       /* va_arg(ap, type) */

    /* 타입 */
    노드_기본타입,
    노드_참조타입,       /* 포인터 */
    노드_배열타입,
    노드_묶음타입명,     /* 사용자 정의 타입 이름 */
    노드_함수포인터타입, /* 함수 포인터 타입: [타입, 타입 → 반환타입] */
} 노드_종류;

/* ===== 전방 선언 ===== */

typedef struct 노드 노드;
typedef struct 매개변수 매개변수;
typedef struct 인자 인자;
typedef struct 묶음필드 묶음필드;
typedef struct 갈래경우 갈래경우;

/* ===== 매개변수 (함수 정의용) ===== */

struct 매개변수 {
    노드 *타입;          /* 타입 노드 */
    char *이름;          /* 매개변수 이름 */
    조사_역할 역할;      /* 이 매개변수에 붙는 조사의 역할 */
    bool 상수;           /* true면 const */
    매개변수 *다음;      /* 링크드 리스트 */
};

/* ===== 호출 인자 (함수 호출용) ===== */

struct 인자 {
    노드 *값;            /* 인자 값 표현식 */
    조사_역할 역할;      /* 조사가 나타내는 역할 */
    인자 *다음;          /* 링크드 리스트 */
};

/* ===== 묶음(struct) 필드 ===== */

struct 묶음필드 {
    노드 *타입;
    char *이름;
    묶음필드 *다음;
};

/* ===== 파이프라인 단계 ===== */

typedef struct 파이프단계 파이프단계;
struct 파이프단계 {
    char *동사;          /* 호출할 함수/동사 이름 */
    인자 *인자목록;      /* 추가 인자 (있으면) */
    어미_기능 연결;      /* 이 단계와 다음 단계를 잇는 어미 */
    소스위치 위치;
    파이프단계 *다음;
};

/* ===== 갈래(switch) 경우 ===== */

struct 갈래경우 {
    노드 *값;              /* 경우(case)의 값, NULL이면 기본(default) */
    노드 **문장목록;
    int 문장_수;
    갈래경우 *다음;
};

/* ===== AST 노드 ===== */

struct 노드 {
    노드_종류 종류;
    소스위치 위치;

    union {
        /* 프로그램 */
        struct {
            노드 **선언목록;
            int 선언_수;
        } 프로그램;

        /* 함수 정의 */
        struct {
            char *이름;
            매개변수 *매개변수목록;
            노드 *반환타입;     /* NULL이면 공허 */
            노드 *본문;         /* 블록 노드 (외부 선언이면 NULL) */
            bool 외부;          /* true면 extern 선언 */
            bool 가변인자;      /* true면 variadic (...) */
            bool 정적;          /* true면 static */
        } 함수정의;

        /* 변수 선언 */
        struct {
            노드 *타입;
            char *이름;
            노드 *초기값;       /* NULL이면 초기화 없음 */
            bool 정적;          /* true면 static */
            bool 상수;          /* true면 const */
            bool 외부;          /* true면 extern */
        } 변수선언;

        /* 묶음(struct) 정의 */
        struct {
            char *이름;
            묶음필드 *필드목록;
        } 묶음정의;

        /* 합침(union) 정의 */
        struct {
            char *이름;
            묶음필드 *필드목록;
        } 합침정의;

        /* 나열(enum) 정의 */
        struct {
            char *이름;
            char **값이름목록;
            int *값목록;        /* 명시적 값 (없으면 자동 증가) */
            int 값_수;
        } 나열정의;

        /* 별칭 (typedef) */
        struct {
            char *이름;         /* 새 타입 이름 */
            노드 *원래타입;     /* 원래 타입 */
        } 별칭정의;

        /* 블록 */
        struct {
            노드 **문장목록;
            int 문장_수;
        } 블록;

        /* 조건문 (-면) */
        struct {
            노드 *조건;         /* 표현식 */
            노드 *참블록;
            노드 *거짓블록;     /* NULL이면 else 없음 */
        } 조건문;

        /* 동안 반복 (-동안) */
        struct {
            노드 *조건;
            노드 *본문;
        } 동안반복;

        /* 범위 반복 (부터~까지) */
        struct {
            char *변수이름;
            노드 *시작값;
            노드 *끝값;
            노드 *본문;
        } 범위반복;

        /* 반복문 (C-style for) */
        struct {
            노드 *초기화;       /* 초기화 문장 (변수선언 또는 표현식) */
            노드 *조건;         /* 반복 조건 */
            노드 *증감;         /* 증감 표현식 */
            노드 *본문;
        } 반복문;

        /* 반복하기 (do-while) */
        struct {
            노드 *본문;
            노드 *조건;
        } 반복하기;

        /* 이동문 (goto) */
        struct {
            char *레이블;
        } 이동문;

        /* 레이블 (label:) */
        struct {
            char *이름;
            노드 *문장;         /* 레이블 뒤의 문장 */
        } 레이블;

        /* 갈래문 (switch) */
        struct {
            노드 *대상;             /* switch 대상 표현식 */
            갈래경우 *경우목록;     /* case/default 링크드 리스트 */
        } 갈래문;

        /* 반환문 */
        struct {
            노드 *값;           /* NULL이면 공허 반환 */
        } 반환문;

        /* 리터럴 */
        struct { long long 값; } 정수_리터럴;
        struct { double 값; } 실수_리터럴;
        struct { char *값; } 문자열_리터럴;
        struct { char 값[8]; } 문자_리터럴;  /* UTF-8 */
        struct { bool 값; } 참거짓_리터럴;

        /* 식별자 */
        struct { char *이름; } 식별자;

        /* 이항 연산 */
        struct {
            토큰_종류 연산자;
            노드 *왼쪽;
            노드 *오른쪽;
        } 이항연산;

        /* 단항 연산 */
        struct {
            토큰_종류 연산자;
            노드 *피연산자;
        } 단항연산;

        /* 함수 호출 (조사 기반) */
        struct {
            char *함수이름;
            인자 *인자목록;
        } 호출;

        /* 멤버 접근 (의 / →) */
        struct {
            노드 *대상;
            char *멤버이름;
            bool 포인터;        /* true이면 → (p->x), false이면 의 (s.x) */
        } 멤버접근;

        /* 배열 접근 */
        struct {
            노드 *배열;
            노드 *인덱스;
        } 배열접근;

        /* 대입 */
        struct {
            노드 *대상;         /* 대입 받을 대상 (식별자/배열접근/멤버접근) */
            노드 *값;
            토큰_종류 연산자;   /* 토큰_대입, 토큰_더하기대입, ... */
        } 대입;

        /* 형변환 (로/으로) */
        struct {
            노드 *값;
            노드 *대상타입;
        } 형변환;

        /* 주소참조 / 역참조 */
        struct {
            노드 *대상;
        } 참조연산;

        /* 크기 (sizeof) */
        struct {
            노드 *대상;         /* 타입 또는 표현식 */
        } 크기연산;

        /* 파이프라인 (-어서/-아서 체인) */
        struct {
            노드 *시작값;       /* 첫 번째 입력 */
            파이프단계 *단계목록;
        } 파이프라인;

        /* 묶음(struct) 리터럴 */
        struct {
            char **필드이름목록;
            노드 **필드값목록;
            int 필드_수;
        } 묶음_리터럴;

        /* 배열 리터럴 */
        struct {
            노드 **원소목록;
            int 원소_수;
        } 배열_리터럴;

        /* 증감 */
        struct {
            노드 *대상;
            bool 증가;     /* true: ++, false: -- */
            bool 전위;     /* true: ++x, false: x++ */
        } 증감;

        /* 삼항 */
        struct {
            노드 *조건;
            노드 *참값;
            노드 *거짓값;
        } 삼항;

        /* 가변시작 */
        struct {
            char *목록이름;      /* va_list 변수 이름 */
            char *매개변수이름;  /* 마지막 고정 매개변수 이름 */
        } 가변시작;

        /* 가변끝 */
        struct {
            char *목록이름;      /* va_list 변수 이름 */
        } 가변끝;

        /* 가변인자 (표현식) */
        struct {
            char *목록이름;      /* va_list 변수 이름 */
            노드 *타입;          /* 가져올 타입 */
        } 가변인자;

        /* 전처리 지시문 */
        struct {
            char *인자;         /* 매크로 이름 또는 조건식 텍스트 */
        } 전처리;

        /* 매크로 정의 (#define) */
        struct {
            char *이름;         /* 매크로 이름 */
            char *값;           /* 매크로 값 (문자열) */
        } 매크로정의;

        /* 표현식 문장 */
        struct {
            노드 *표현식;
            어미_기능 어미;     /* 이 문장의 어미 */
        } 표현식문;

        /* 타입 노드 */
        struct {
            토큰_종류 기본;     /* 토큰_정수, 토큰_실수 등 */
        } 기본타입;

        struct {
            노드 *대상타입;
        } 참조타입;

        struct {
            노드 *원소타입;
            int 크기;
        } 배열타입;

        struct {
            char *이름;
        } 묶음타입명;

        /* 함수 포인터 타입: [매개변수타입들 → 반환타입] */
        struct {
            노드 **매개변수타입들;
            int   매개변수타입_수;
            노드 *반환타입;
        } 함수포인터타입;
    } 데이터;
};

/* ===== 노드 생성 함수 ===== */

노드 *노드_생성(노드_종류 종류, 소스위치 위치);

/* 매개변수, 인자, 필드 생성 */
매개변수 *매개변수_생성(노드 *타입, const char *이름, 조사_역할 역할);
인자 *인자_생성(노드 *값, 조사_역할 역할);
묶음필드 *필드_생성(노드 *타입, const char *이름);
파이프단계 *파이프단계_생성(const char *동사, 어미_기능 연결, 소스위치 위치);

/* 문자열 복사 유틸리티 */
char *문자열_복사(const char *원본);
char *문자열_복사n(const char *원본, size_t 길이);

/* ===== 노드 해제 ===== */

void 노드_해제(노드 *n);

/* ===== 디버깅용 AST 출력 ===== */

void ast_출력(노드 *n, int 들여쓰기);

#endif /* GEUL_AST_H */
