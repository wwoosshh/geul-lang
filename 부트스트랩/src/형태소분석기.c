static bool 분리제외_단어인가(const char *단어) {
    for (int i = 0; 분리제외_사전[i] != NULL; i++) {
        if (strcmp(단어, 분리제외_사전[i]) == 0) return true;
    }
    return false;
}

/* 주어진 문자열(길이 지정)이 분리제외 단어인지 확인 */
static bool 분리제외_어근인가(const char *시작, size_t 길이) {
    char 임시[64];
    if (길이 >= sizeof(임시)) return false;
    memcpy(임시, 시작, 길이);
    임시[길이] = '\0';
    return 분리제외_단어인가(임시);
}

