/* 현재 위치의 코드포인트를 읽되 포인터를 전진하지 않음 */
static 코드포인트 현재_코드포인트(형태소분석기 *분석기) {
    if (분석기->현재 >= 분석기->소스끝) return 0;
    const char *tmp = 분석기->현재;
    return utf8_디코딩(&tmp);
}

/* 현재 위치의 코드포인트를 읽고 전진 */
static 코드포인트 코드포인트_읽기(형태소분석기 *분석기) {
    if (분석기->현재 >= 분석기->소스끝) return 0;
    코드포인트 cp = utf8_디코딩(&분석기->현재);
    if (cp == '\n') {
        분석기->줄++;
        분석기->열 = 1;
    } else {
        분석기->열++;
    }
    return cp;
}

/* 토큰 생성 헬퍼 */
static 토큰 토큰_생성(토큰_종류 종류, const char *텍스트, size_t 길이, 소스위치 위치) {
    토큰 t = {0};
    t.종류 = 종류;
    t.텍스트 = (char *)malloc(길이 + 1);
    memcpy(t.텍스트, 텍스트, 길이);
    t.텍스트[길이] = '\0';
    t.텍스트_길이 = 길이;
    t.역할 = 조사_역할_분류(종류);
    t.기능 = 어미_기능_분류(종류);
    t.위치 = 위치;
    return t;
}

/* 오류 토큰 생성 */
static 토큰 오류_토큰(형태소분석기 *분석기, const char *메시지) {
    snprintf(분석기->오류_메시지, sizeof(분석기->오류_메시지),
             "%s:%d:%d: 오류: %s", 분석기->파일명, 분석기->줄, 분석기->열, 메시지);
    분석기->오류_발생 = true;
    소스위치 위치 = { 분석기->파일명, 분석기->줄, 분석기->열 };
    return 토큰_생성(토큰_오류, 메시지, strlen(메시지), 위치);
}

/* 키워드 검색 */
static 토큰_종류 키워드_찾기(const char *텍스트, size_t 길이) {
    for (int i = 0; 키워드_테이블[i].텍스트 != NULL; i++) {
        if (strlen(키워드_테이블[i].텍스트) == 길이 &&
            memcmp(키워드_테이블[i].텍스트, 텍스트, 길이) == 0) {
            return 키워드_테이블[i].종류;
        }
    }
    return 토큰_식별자; /* 키워드가 아니면 식별자 */
}

/* ===== 공백/주석 건너뛰기 ===== */

static void 공백_건너뛰기(형태소분석기 *분석기) {
    while (분석기->현재 < 분석기->소스끝) {
        코드포인트 cp = 현재_코드포인트(분석기);

        if (cp == ' ' || cp == '\t' || cp == '\r' || cp == 0x3000) {
            코드포인트_읽기(분석기);
            continue;
        }

        /* 줄 주석: (* ... *) 또는 // */
        if (cp == '/' && 분석기->현재 + 1 < 분석기->소스끝 &&
            *(분석기->현재 + 1) == '/') {
            /* // 스타일 주석: 줄 끝까지 건너뛰기 */
            while (분석기->현재 < 분석기->소스끝 && *분석기->현재 != '\n') {
                분석기->현재++;
            }
            continue;
        }

        /* 블록 주석: (* ... *) */
        if (cp == '(' && 분석기->현재 + 1 < 분석기->소스끝 &&
            *(분석기->현재 + 1) == '*') {
            분석기->현재 += 2;
            분석기->열 += 2;
            int 깊이 = 1;
            while (분석기->현재 < 분석기->소스끝 && 깊이 > 0) {
                if (*분석기->현재 == '(' && 분석기->현재 + 1 < 분석기->소스끝 &&
                    *(분석기->현재 + 1) == '*') {
                    깊이++;
                    분석기->현재 += 2;
                    분석기->열 += 2;
                } else if (*분석기->현재 == '*' && 분석기->현재 + 1 < 분석기->소스끝 &&
                           *(분석기->현재 + 1) == ')') {
                    깊이--;
                    분석기->현재 += 2;
                    분석기->열 += 2;
                } else {
                    if (*분석기->현재 == '\n') {
                        분석기->줄++;
                        분석기->열 = 1;
                    } else {
                        분석기->열++;
                    }
                    분석기->현재++;
                }
            }
            continue;
        }

        break; /* 공백/주석이 아님 */
    }
}

/* ===== 숫자 리터럴 읽기 ===== */

static 토큰 숫자_읽기(형태소분석기 *분석기) {
    소스위치 위치 = { 분석기->파일명, 분석기->줄, 분석기->열 };
    const char *시작 = 분석기->현재;
    bool 실수 = false;

    /* 0x, 0b, 0o 접두사 */
    if (*분석기->현재 == '0' && 분석기->현재 + 1 < 분석기->소스끝) {
        char 다음 = *(분석기->현재 + 1);
        if (다음 == 'x' || 다음 == 'X') {
            분석기->현재 += 2;
            분석기->열 += 2;
            while (분석기->현재 < 분석기->소스끝 &&
                   ((*분석기->현재 >= '0' && *분석기->현재 <= '9') ||
                    (*분석기->현재 >= 'a' && *분석기->현재 <= 'f') ||
                    (*분석기->현재 >= 'A' && *분석기->현재 <= 'F') ||
                    *분석기->현재 == '_')) {
                분석기->현재++;
                분석기->열++;
            }
            토큰 t = 토큰_생성(토큰_정수_리터럴, 시작, 분석기->현재 - 시작, 위치);
            t.값.정수값 = strtoll(시작, NULL, 16);
            return t;
        }
        if (다음 == 'b' || 다음 == 'B') {
            분석기->현재 += 2;
            분석기->열 += 2;
            while (분석기->현재 < 분석기->소스끝 &&
                   (*분석기->현재 == '0' || *분석기->현재 == '1' || *분석기->현재 == '_')) {
                분석기->현재++;
                분석기->열++;
            }
            토큰 t = 토큰_생성(토큰_정수_리터럴, 시작, 분석기->현재 - 시작, 위치);
            t.값.정수값 = strtoll(시작 + 2, NULL, 2);
            return t;
        }
    }

    /* 10진 정수 또는 실수 */
    while (분석기->현재 < 분석기->소스끝 &&
           ((*분석기->현재 >= '0' && *분석기->현재 <= '9') || *분석기->현재 == '_')) {
        분석기->현재++;
        분석기->열++;
    }

    /* 소수점 */
    if (분석기->현재 < 분석기->소스끝 && *분석기->현재 == '.') {
        const char *다음 = 분석기->현재 + 1;
        if (다음 < 분석기->소스끝 && *다음 >= '0' && *다음 <= '9') {
            실수 = true;
            분석기->현재++; /* '.' */
            분석기->열++;
            while (분석기->현재 < 분석기->소스끝 &&
                   ((*분석기->현재 >= '0' && *분석기->현재 <= '9') || *분석기->현재 == '_')) {
                분석기->현재++;
                분석기->열++;
            }
        }
    }

    /* 지수 */
    if (분석기->현재 < 분석기->소스끝 &&
        (*분석기->현재 == 'e' || *분석기->현재 == 'E')) {
        실수 = true;
        분석기->현재++;
        분석기->열++;
        if (분석기->현재 < 분석기->소스끝 &&
            (*분석기->현재 == '+' || *분석기->현재 == '-')) {
            분석기->현재++;
            분석기->열++;
        }
        while (분석기->현재 < 분석기->소스끝 && *분석기->현재 >= '0' && *분석기->현재 <= '9') {
            분석기->현재++;
            분석기->열++;
        }
    }

    size_t 길이 = 분석기->현재 - 시작;
    if (실수) {
        토큰 t = 토큰_생성(토큰_실수_리터럴, 시작, 길이, 위치);
        t.값.실수값 = strtod(시작, NULL);
        return t;
    } else {
        토큰 t = 토큰_생성(토큰_정수_리터럴, 시작, 길이, 위치);
        t.값.정수값 = strtoll(시작, NULL, 10);
        return t;
    }
}

/* ===== 문자열 리터럴 읽기 ===== */

static 토큰 문자열_읽기(형태소분석기 *분석기) {
    소스위치 위치 = { 분석기->파일명, 분석기->줄, 분석기->열 };
    const char *시작 = 분석기->현재;

    분석기->현재++; /* 여는 " 건너뛰기 */
    분석기->열++;

    while (분석기->현재 < 분석기->소스끝 && *분석기->현재 != '"') {
        if (*분석기->현재 == '\\') {
            분석기->현재++; /* 이스케이프 문자 건너뛰기 */
            분석기->열++;
        }
        if (*분석기->현재 == '\n') {
            분석기->줄++;
            분석기->열 = 1;
        } else {
            분석기->열++;
        }
        분석기->현재++;
    }

    if (분석기->현재 >= 분석기->소스끝) {
        return 오류_토큰(분석기, "문자열이 닫히지 않았습니다");
    }

    분석기->현재++; /* 닫는 " 건너뛰기 */
    분석기->열++;

    return 토큰_생성(토큰_문자열_리터럴, 시작, 분석기->현재 - 시작, 위치);
}

/* ===== 문자 리터럴 읽기 ===== */

static 토큰 문자_리터럴_읽기(형태소분석기 *분석기) {
    소스위치 위치 = { 분석기->파일명, 분석기->줄, 분석기->열 };
    const char *시작 = 분석기->현재;

    분석기->현재++; /* 여는 ' */
    분석기->열++;

    if (*분석기->현재 == '\\') {
        분석기->현재 += 2; /* 이스케이프 */
        분석기->열 += 2;
    } else {
        /* 한글 문자는 UTF-8로 3바이트 */
        const char *tmp = 분석기->현재;
        utf8_디코딩(&tmp);
        분석기->열++;
        분석기->현재 = tmp;
    }

    if (분석기->현재 >= 분석기->소스끝 || *분석기->현재 != '\'') {
        return 오류_토큰(분석기, "문자 리터럴이 닫히지 않았습니다");
    }

    분석기->현재++; /* 닫는 ' */
    분석기->열++;

    return 토큰_생성(토큰_문자_리터럴, 시작, 분석기->현재 - 시작, 위치);
}

/* ===== 한글 토큰 읽기 + 형태소 분리 (핵심) ===== */

/*
 * 연속된 한글+숫자+밑줄 문자열을 읽고,
 * 키워드 매칭 → 접사 분리 → 동사형 분석 순서로 처리한다.
 *
 * 결과 토큰(들)은 분석기의 버퍼에 넣어진다.
 */
static void 한글_토큰_처리(형태소분석기 *분석기) {
    소스위치 위치 = { 분석기->파일명, 분석기->줄, 분석기->열 };
    const char *시작 = 분석기->현재;

    /* 연속된 식별자 문자를 모두 읽음 */
    /* 단, ASCII→한글 경계에서 분리 (영문 식별자 뒤에 한글 조사/어미 방지) */
    bool 영문_시작 = (*시작 >= 'A' && *시작 <= 'Z') ||
                      (*시작 >= 'a' && *시작 <= 'z') || *시작 == '_';
    bool 밑줄_있음 = false;
    bool 한글_허용 = false;  /* 하/해/한 발견 후 후속 한글 허용 */

    while (분석기->현재 < 분석기->소스끝) {
        const char *tmp = 분석기->현재;
        코드포인트 cp = utf8_디코딩(&tmp);
        if (!식별자_문자인가(cp)) break;

        /* 밑줄 발견 시 플래그 설정 */
        if (cp == '_') 밑줄_있음 = true;

        /* 영문으로 시작한 식별자가 한글을 만나면 여기서 끊음 */
        /* 단, 밑줄이 포함된 식별자는 끊지 않음: utf8_디코딩 등 */
        /* 단, "하"(U+D558)/"해"(U+D574)/"한"(U+D55C) 동사형은 끊지 않음 */
        /* 하/해/한 발견 시 한글_허용 플래그를 세워서 이후 어미도 포함 */
        /* 예: printf하다 → 하나의 단어로 읽어 동사형 분리 처리 */
        if (영문_시작 && 한글인가(cp) && !밑줄_있음 && !한글_허용) {
            if (cp == 0xD558 || cp == 0xD574 || cp == 0xD55C)
                한글_허용 = true;
            else
                break;
        }

        분석기->현재 = tmp;
        분석기->열++;
    }

    size_t 단어_길이 = 분석기->현재 - 시작;
    char 단어[최대_토큰_길이];
    if (단어_길이 >= 최대_토큰_길이) 단어_길이 = 최대_토큰_길이 - 1;
    memcpy(단어, 시작, 단어_길이);
    단어[단어_길이] = '\0';

    /* 0단계: 단어 전체가 조사/어미인지 확인 (공백으로 분리된 경우) */
    for (int i = 0; 접사_테이블[i].텍스트 != NULL; i++) {
        if ((int)단어_길이 == 접사_테이블[i].바이트수 &&
            memcmp(단어, 접사_테이블[i].텍스트, 단어_길이) == 0) {
            분석기->버퍼[분석기->버퍼_수++] =
                토큰_생성(접사_테이블[i].종류, 시작, 단어_길이, 위치);
            return;
        }
    }

    /* 1단계: 전체가 키워드인지 확인 */
    토큰_종류 키워드 = 키워드_찾기(단어, 단어_길이);
    if (키워드 != 토큰_식별자) {
        분석기->버퍼[분석기->버퍼_수++] = 토큰_생성(키워드, 시작, 단어_길이, 위치);
        return;
    }

    /* 1.5단계: 분리 제외 사전 확인
     * 전체 단어가 분리제외 사전에 있으면 접사 분리 없이 식별자로 처리.
     * 예: "나이" → 식별자 (분리 안 함)
     * 단, "나이를" → 여기서 안 걸림 → 2단계에서 "나이"+"를"로 정상 분리 */
    if (분리제외_단어인가(단어)) {
        분석기->버퍼[분석기->버퍼_수++] = 토큰_생성(토큰_식별자, 시작, 단어_길이, 위치);
        return;
    }

    /* 2단계: 뒤에서부터 접사(조사/어미) 분리 시도 */
    for (int i = 0; 접사_테이블[i].텍스트 != NULL; i++) {
        int 접사_길이 = 접사_테이블[i].바이트수;

        if ((int)단어_길이 <= 접사_길이) continue; /* 어근이 남아야 함 */

        /* 단어 끝이 접사와 일치하는지 */
        if (memcmp(단어 + 단어_길이 - 접사_길이,
                   접사_테이블[i].텍스트, 접사_길이) == 0) {

            size_t 어근_길이 = 단어_길이 - 접사_길이;

            /* 어근이 키워드이면 분리 확정 */
            토큰_종류 어근_종류 = 키워드_찾기(시작, 어근_길이);

            /* 동사형 확인: 어근이 "~하"로 끝나면 동사어근 분리 */
            bool 동사형 = false;
            size_t 동사어근_길이 = 0;
            /* "하"는 UTF-8로 3바이트: 0xED 0x95 0x98 */
            if (어근_길이 >= 3 &&
                (unsigned char)시작[어근_길이 - 3] == 0xED &&
                (unsigned char)시작[어근_길이 - 2] == 0x95 &&
                (unsigned char)시작[어근_길이 - 1] == 0x98) {
                동사형 = true;
                동사어근_길이 = 어근_길이 - 3; /* "하" 제거 */
            }
            /* "하"가 변형된 "해" (0xED 0x95 0xB4), "하" 뒤 어미에 따른 변형도 체크 */
            /* "해"는 "하" + "여"의 축약형 */
            if (어근_길이 >= 3 &&
                (unsigned char)시작[어근_길이 - 3] == 0xED &&
                (unsigned char)시작[어근_길이 - 2] == 0x95 &&
                (unsigned char)시작[어근_길이 - 1] == 0xB4) {
                동사형 = true;
                동사어근_길이 = 어근_길이 - 3; /* "해" 제거 */
            }
            /* "한" (0xED 0x95 0x9C) - "하" + "ㄴ" 결합형 */
            /* 예: 계산한다 → 계산 + 한 + 다 → "계산" + "다" */
            if (어근_길이 >= 3 &&
                (unsigned char)시작[어근_길이 - 3] == 0xED &&
                (unsigned char)시작[어근_길이 - 2] == 0x95 &&
                (unsigned char)시작[어근_길이 - 1] == 0x9C) {
                동사형 = true;
                동사어근_길이 = 어근_길이 - 3; /* "한" 제거 */
            }

            if (어근_종류 != 토큰_식별자) {
                /* 키워드 + 접사 */
                분석기->버퍼[분석기->버퍼_수++] =
                    토큰_생성(어근_종류, 시작, 어근_길이, 위치);
                소스위치 접사위치 = { 분석기->파일명, 위치.줄, 위치.열 + (int)utf8_길이(시작) };
                분석기->버퍼[분석기->버퍼_수++] =
                    토큰_생성(접사_테이블[i].종류,
                             시작 + 어근_길이, 접사_길이, 접사위치);
                return;
            }

            /* 어근이 접사 테이블 항목과 정확히 일치하면 접사+접사 분리
             * 예: "이면" → "이"(조사_이) + "면"(어미_면)
             *     "이다" → "이"(조사_이) + "다"(어미_다)
             *     "이고" → "이"(조사_이) + "고"(어미_고) */
            for (int j = 0; 접사_테이블[j].텍스트 != NULL; j++) {
                if ((int)어근_길이 == 접사_테이블[j].바이트수 &&
                    memcmp(시작, 접사_테이블[j].텍스트, 어근_길이) == 0) {
                    /* 어근 자체가 접사 → 접사+접사 두 토큰 발행 */
                    분석기->버퍼[분석기->버퍼_수++] =
                        토큰_생성(접사_테이블[j].종류, 시작, 어근_길이, 위치);
                    소스위치 접사위치 = { 분석기->파일명, 위치.줄,
                        위치.열 + (int)어근_길이 / 3 };
                    분석기->버퍼[분석기->버퍼_수++] =
                        토큰_생성(접사_테이블[i].종류,
                                 시작 + 어근_길이, 접사_길이, 접사위치);
                    return;
                }
            }

            if (동사형 && 동사어근_길이 > 0) {
                /* 동사어근 + "하"/"해" + 어미 — 어근이 키워드이면 키워드 종류 사용 */
                토큰_종류 동사어근_종류 = 키워드_찾기(시작, 동사어근_길이);
                if (동사어근_종류 == 토큰_식별자) 동사어근_종류 = 토큰_식별자;
                분석기->버퍼[분석기->버퍼_수++] =
                    토큰_생성(동사어근_종류, 시작, 동사어근_길이, 위치);
                소스위치 접사위치 = { 분석기->파일명, 위치.줄, 위치.열 + (int)utf8_길이(시작) };
                분석기->버퍼[분석기->버퍼_수++] =
                    토큰_생성(접사_테이블[i].종류,
                             시작 + 어근_길이, 접사_길이, 접사위치);
                return;
            }

            /* 어근에 밑줄(_)이 포함된 복합 식별자는 접사 분리를 하지 않음.
             * 예: 글_길이 → 그대로 유지 (글_길 + 이 로 분리 방지)
             *     종류_문자 → 그대로 유지 (종류_문 + 자 로 분리 방지)
             *     종류_보다 → 그대로 유지 (종류_보 + 다 로 분리 방지)
             * 밑줄 함수 호출은 직접 호출 구문 사용: 함수이름(인자) */
            bool 밑줄_포함 = false;
            for (size_t k = 0; k < 어근_길이; k++) {
                if (시작[k] == '_') { 밑줄_포함 = true; break; }
            }
            if (밑줄_포함) continue;

            /* 2.5단계: 서술격 조사 "이다" 패턴 처리
             * 어미(다,면,고...) 앞에 "이"가 붙어있으면 추가 분리
             * 예: 할당이다 → 할당 + 이 + 다, 할당이면 → 할당 + 이 + 면
             * 단, 밑줄 포함 식별자(글_길이 등)는 보호 */
            if (어미_토큰인가(접사_테이블[i].종류) && 어근_길이 >= 6 && !밑줄_포함) {
                /* "이"(U+C774)는 UTF-8: 0xEC 0x9D 0xB4 */
                const unsigned char *끝 = (const unsigned char *)시작 + 어근_길이;
                if (끝[-3] == 0xEC && 끝[-2] == 0x9D && 끝[-1] == 0xB4) {
                    size_t 내부어근_길이 = 어근_길이 - 3;
                    /* 분리 제외 확인: 어근이 보호 단어이면 이+어미 분리 안 함
                     * 예: "나이다" → 어근 "나이"가 제외 단어 → "나이"+"다"로 분리
                     *     "할당이다" → 어근 "할당이"에서 "할당" 비제외 → "할당"+"이"+"다" */
                    if (내부어근_길이 >= 3 && !분리제외_어근인가(시작, 어근_길이)) {
                        토큰_종류 내부종류 = 키워드_찾기(시작, 내부어근_길이);
                        if (내부종류 == 토큰_식별자) 내부종류 = 토큰_식별자;
                        분석기->버퍼[분석기->버퍼_수++] =
                            토큰_생성(내부종류, 시작, 내부어근_길이, 위치);
                        소스위치 이_위치 = { 분석기->파일명, 위치.줄,
                            위치.열 + (int)내부어근_길이 / 3 };
                        분석기->버퍼[분석기->버퍼_수++] =
                            토큰_생성(토큰_조사_이, 시작 + 내부어근_길이, 3, 이_위치);
                        소스위치 어미위치 = { 분석기->파일명, 위치.줄,
                            위치.열 + (int)어근_길이 / 3 };
                        분석기->버퍼[분석기->버퍼_수++] =
                            토큰_생성(접사_테이블[i].종류,
                                     시작 + 어근_길이, 접사_길이, 어미위치);
                        return;
                    }
                }
            }

            /* 3단계: 어근이 식별자일 때, 내부에 키워드+조사 패턴이 있으면 재분리
             * 예: 정수로다 → 정수(키워드) + 로(조사) + 다(어미)
             * 어근 "정수로"를 다시 접사 테이블로 검사 */
            {
                bool 재분리됨 = false;
                for (int j = 0; 접사_테이블[j].텍스트; j++) {
                    size_t 내부접사_길이 = 접사_테이블[j].바이트수;
                    if (내부접사_길이 >= 어근_길이) continue;
                    size_t 내부어근_길이 = 어근_길이 - 내부접사_길이;
                    if (내부어근_길이 < 3) continue;
                    if (memcmp(시작 + 내부어근_길이, 접사_테이블[j].텍스트, 내부접사_길이) != 0) continue;
                    토큰_종류 내부어근_종류 = 키워드_찾기(시작, 내부어근_길이);
                    if (내부어근_종류 == 토큰_식별자) continue; /* 키워드가 아니면 건너뜀 */
                    /* 키워드 + 조사 + 원래접사 → 3토큰 분리 */
                    분석기->버퍼[분석기->버퍼_수++] =
                        토큰_생성(내부어근_종류, 시작, 내부어근_길이, 위치);
                    소스위치 내부접사위치 = { 분석기->파일명, 위치.줄,
                        위치.열 + (int)내부어근_길이 / 3 };
                    분석기->버퍼[분석기->버퍼_수++] =
                        토큰_생성(접사_테이블[j].종류,
                                 시작 + 내부어근_길이, 내부접사_길이, 내부접사위치);
                    소스위치 접사위치 = { 분석기->파일명, 위치.줄,
                        위치.열 + (int)어근_길이 / 3 };
                    분석기->버퍼[분석기->버퍼_수++] =
                        토큰_생성(접사_테이블[i].종류,
                                 시작 + 어근_길이, 접사_길이, 접사위치);
                    재분리됨 = true;
                    break;
                }
                if (재분리됨) return;
            }

            /* 어근 끝부분 + 접사가 분리제외 단어를 형성하면 분리 금지
             * 예: "전체길이" → 어근 "전체길" + 접사 "이"
             *      "길"+"이" = "길이" → 분리제외 사전에 있음 → 전체를 식별자로 유지 */
            {
                bool 부분제외 = false;
                for (int k = 0; 분리제외_사전[k] != NULL; k++) {
                    size_t 제외_길이 = strlen(분리제외_사전[k]);
                    if (제외_길이 <= 접사_길이) continue;      /* 제외 단어가 접사보다 길어야 함 */
                    if (제외_길이 > 어근_길이 + 접사_길이) continue; /* 전체 단어보다 길면 불가 */
                    /* 제외 단어의 뒷부분이 현재 접사와 일치하는지 확인 */
                    size_t 제외_어근부분 = 제외_길이 - 접사_길이;
                    if (memcmp(분리제외_사전[k] + 제외_어근부분,
                               접사_테이블[i].텍스트, 접사_길이) != 0) continue;
                    /* 어근의 끝부분이 제외 단어의 앞부분과 일치하는지 확인 */
                    if (어근_길이 >= 제외_어근부분 &&
                        memcmp(시작 + 어근_길이 - 제외_어근부분,
                               분리제외_사전[k], 제외_어근부분) == 0) {
                        부분제외 = true;
                        break;
                    }
                }
                if (부분제외) {
                    /* 분리하지 않고 전체를 식별자로 처리 */
                    분석기->버퍼[분석기->버퍼_수++] =
                        토큰_생성(토큰_식별자, 시작, 단어_길이, 위치);
                    return;
                }
            }

            분석기->버퍼[분석기->버퍼_수++] =
                토큰_생성(토큰_식별자, 시작, 어근_길이, 위치);
            소스위치 접사위치 = { 분석기->파일명, 위치.줄, 위치.열 + (int)어근_길이 / 3 };
            분석기->버퍼[분석기->버퍼_수++] =
                토큰_생성(접사_테이블[i].종류,
                         시작 + 어근_길이, 접사_길이, 접사위치);
            return;
        }
    }

    /* 3단계: 접사 분리 실패 → 전체를 식별자로 처리 */
    분석기->버퍼[분석기->버퍼_수++] = 토큰_생성(토큰_식별자, 시작, 단어_길이, 위치);
}

/* ===== 기호/연산자 읽기 ===== */

static 토큰 기호_읽기(형태소분석기 *분석기) {
    소스위치 위치 = { 분석기->파일명, 분석기->줄, 분석기->열 };
    char c = *분석기->현재;
    const char *시작 = 분석기->현재;

    분석기->현재++;
    분석기->열++;

    /* 2문자 연산자 확인 */
    if (분석기->현재 < 분석기->소스끝) {
        char c2 = *분석기->현재;
        switch (c) {
        case '=':
            if (c2 == '=') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_같다, 시작, 2, 위치); }
            return 토큰_생성(토큰_대입, 시작, 1, 위치);
        case '!':
            if (c2 == '=') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_다르다, 시작, 2, 위치); }
            return 토큰_생성(토큰_아닌, 시작, 1, 위치);
        case '<':
            if (c2 == '=') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_작거나같다, 시작, 2, 위치); }
            if (c2 == '<') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_왼쪽이동, 시작, 2, 위치); }
            return 토큰_생성(토큰_작다, 시작, 1, 위치);
        case '>':
            if (c2 == '=') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_크거나같다, 시작, 2, 위치); }
            if (c2 == '>') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_오른쪽이동, 시작, 2, 위치); }
            return 토큰_생성(토큰_크다, 시작, 1, 위치);
        case '&':
            if (c2 == '&') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_그리고, 시작, 2, 위치); }
            return 토큰_생성(토큰_비트와, 시작, 1, 위치);
        case '|':
            if (c2 == '|') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_또는, 시작, 2, 위치); }
            return 토큰_생성(토큰_비트또는, 시작, 1, 위치);
        case '-':
            if (c2 == '>') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_화살표, 시작, 2, 위치); }
            if (c2 == '-') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_감소, 시작, 2, 위치); }
            if (c2 == '=') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_빼기대입, 시작, 2, 위치); }
            return 토큰_생성(토큰_빼기, 시작, 1, 위치);
        case '+':
            if (c2 == '+') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_증가, 시작, 2, 위치); }
            if (c2 == '=') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_더하기대입, 시작, 2, 위치); }
            return 토큰_생성(토큰_더하기, 시작, 1, 위치);
        case '*':
            if (c2 == '=') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_곱하기대입, 시작, 2, 위치); }
            return 토큰_생성(토큰_곱하기, 시작, 1, 위치);
        case '/':
            if (c2 == '=') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_나누기대입, 시작, 2, 위치); }
            return 토큰_생성(토큰_나누기, 시작, 1, 위치);
        case '%':
            if (c2 == '=') { 분석기->현재++; 분석기->열++; return 토큰_생성(토큰_나머지대입, 시작, 2, 위치); }
            return 토큰_생성(토큰_나머지, 시작, 1, 위치);
        default:
            break;
        }
    }

    /* 1문자 토큰 */
    switch (c) {
    case '(': return 토큰_생성(토큰_여는괄호, 시작, 1, 위치);
    case ')': return 토큰_생성(토큰_닫는괄호, 시작, 1, 위치);
    case '{': return 토큰_생성(토큰_여는중괄호, 시작, 1, 위치);
    case '}': return 토큰_생성(토큰_닫는중괄호, 시작, 1, 위치);
    case '[': return 토큰_생성(토큰_여는대괄호, 시작, 1, 위치);
    case ']': return 토큰_생성(토큰_닫는대괄호, 시작, 1, 위치);
    case ',': return 토큰_생성(토큰_쉼표, 시작, 1, 위치);
    case '.': return 토큰_생성(토큰_마침표, 시작, 1, 위치);
    case ':': return 토큰_생성(토큰_쌍점, 시작, 1, 위치);
    case '?': return 토큰_생성(토큰_물음표, 시작, 1, 위치);
    /* +, -, *, /, %는 2문자 연산자 섹션에서 처리 (+=, -= 등) */
    case '^': return 토큰_생성(토큰_비트배타, 시작, 1, 위치);
    case '~': return 토큰_생성(토큰_비트아닌, 시작, 1, 위치);
    case '=': return 토큰_생성(토큰_대입, 시작, 1, 위치);
    case '#': return 토큰_생성(토큰_전처리, 시작, 1, 위치);
    case '\n': return 토큰_생성(토큰_줄바꿈, 시작, 1, 위치);
    default:
        break;
    }

    /* → (U+2192) 화살표 문자 확인 */
    분석기->현재 = 시작; /* 되감기 */
    const char *tmp = 분석기->현재;
    코드포인트 cp = utf8_디코딩(&tmp);
    if (cp == 0x2192) { /* → */
        분석기->현재 = tmp;
        분석기->열++;
        return 토큰_생성(토큰_화살표, 시작, tmp - 시작, 위치);
    }

    /* 알 수 없는 문자 */
    분석기->현재 = tmp;
    분석기->열++;
    return 오류_토큰(분석기, "알 수 없는 문자입니다");
}

/* ===== 공개 API ===== */

void 분석기_초기화(형태소분석기 *분석기, const char *소스, size_t 길이,
                   const char *파일명) {
    memset(분석기, 0, sizeof(형태소분석기));
    분석기->소스 = 소스;
    분석기->현재 = 소스;
    분석기->소스끝 = 소스 + 길이;
    분석기->파일명 = 파일명;
    분석기->줄 = 1;
    분석기->열 = 1;
    분석기->버퍼_수 = 0;
    분석기->버퍼_위치 = 0;
    분석기->오류_발생 = false;

    접사_테이블_초기화();
}

토큰 다음_토큰(형태소분석기 *분석기) {
    /* 버퍼에 대기 중인 토큰이 있으면 그것을 반환 */
    if (분석기->버퍼_위치 < 분석기->버퍼_수) {
        return 분석기->버퍼[분석기->버퍼_위치++];
    }

    /* 버퍼 초기화 */
    분석기->버퍼_수 = 0;
    분석기->버퍼_위치 = 0;

    공백_건너뛰기(분석기);

    if (분석기->현재 >= 분석기->소스끝) {
        소스위치 위치 = { 분석기->파일명, 분석기->줄, 분석기->열 };
        return 토큰_생성(토큰_파일끝, "", 0, 위치);
    }

    const char *tmp = 분석기->현재;
    코드포인트 cp = utf8_디코딩(&tmp);

    /* 줄바꿈 */
    if (cp == '\n') {
        소스위치 위치 = { 분석기->파일명, 분석기->줄, 분석기->열 };
        코드포인트_읽기(분석기);
        return 토큰_생성(토큰_줄바꿈, "\n", 1, 위치);
    }

    /* 숫자 리터럴 */
    if (숫자인가(cp)) {
        return 숫자_읽기(분석기);
    }

    /* 문자열 리터럴 */
    if (cp == '"') {
        return 문자열_읽기(분석기);
    }

    /* 문자 리터럴 */
    if (cp == '\'') {
        return 문자_리터럴_읽기(분석기);
    }

    /* 한글/식별자 → 형태소 분석 */
    if (식별자_시작인가(cp)) {
        한글_토큰_처리(분석기);
        return 분석기->버퍼[분석기->버퍼_위치++];
    }

    /* 기호/연산자 */
    return 기호_읽기(분석기);
}

토큰 미리보기(형태소분석기 *분석기) {
    /* 버퍼에 토큰이 있으면 미리보기 */
    if (분석기->버퍼_위치 < 분석기->버퍼_수) {
        return 분석기->버퍼[분석기->버퍼_위치];
    }

    /* 상태 저장 */
    const char *저장_현재 = 분석기->현재;
    int 저장_줄 = 분석기->줄;
    int 저장_열 = 분석기->열;
    int 저장_버퍼수 = 분석기->버퍼_수;
    int 저장_버퍼위치 = 분석기->버퍼_위치;

    토큰 t = 다음_토큰(분석기);

    /* 상태 복원 */
    분석기->현재 = 저장_현재;
    분석기->줄 = 저장_줄;
    분석기->열 = 저장_열;
    분석기->버퍼_수 = 저장_버퍼수;
    분석기->버퍼_위치 = 저장_버퍼위치;

    return t;
}
