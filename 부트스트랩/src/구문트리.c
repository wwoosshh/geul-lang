/*
 * 구문트리.c - AST 노드 생성/해제/출력 구현
 */
#include "구문트리.h"
#include <stdlib.h>
#include <string.h>
#include <stdio.h>

/* ===== 유틸리티 ===== */

char *문자열_복사(const char *원본) {
    if (!원본) return NULL;
    size_t len = strlen(원본);
    char *복사본 = (char *)malloc(len + 1);
    memcpy(복사본, 원본, len + 1);
    return 복사본;
}

char *문자열_복사n(const char *원본, size_t 길이) {
    if (!원본) return NULL;
    char *복사본 = (char *)malloc(길이 + 1);
    memcpy(복사본, 원본, 길이);
    복사본[길이] = '\0';
    return 복사본;
}

/* ===== 노드 생성 ===== */

노드 *노드_생성(노드_종류 종류, 소스위치 위치) {
    노드 *n = (노드 *)calloc(1, sizeof(노드));
    n->종류 = 종류;
    n->위치 = 위치;
    return n;
}

매개변수 *매개변수_생성(노드 *타입, const char *이름, 조사_역할 역할) {
    매개변수 *p = (매개변수 *)calloc(1, sizeof(매개변수));
    p->타입 = 타입;
    p->이름 = 문자열_복사(이름);
    p->역할 = 역할;
    p->다음 = NULL;
    return p;
}

인자 *인자_생성(노드 *값, 조사_역할 역할) {
    인자 *a = (인자 *)calloc(1, sizeof(인자));
    a->값 = 값;
    a->역할 = 역할;
    a->다음 = NULL;
    return a;
}

묶음필드 *필드_생성(노드 *타입, const char *이름) {
    묶음필드 *f = (묶음필드 *)calloc(1, sizeof(묶음필드));
    f->타입 = 타입;
    f->이름 = 문자열_복사(이름);
    f->다음 = NULL;
    return f;
}

파이프단계 *파이프단계_생성(const char *동사, 어미_기능 연결, 소스위치 위치) {
    파이프단계 *s = (파이프단계 *)calloc(1, sizeof(파이프단계));
    s->동사 = 문자열_복사(동사);
    s->연결 = 연결;
    s->위치 = 위치;
    s->인자목록 = NULL;
    s->다음 = NULL;
    return s;
}

/* ===== 노드 해제 ===== */

static void 매개변수_해제(매개변수 *p) {
    while (p) {
        매개변수 *다음 = p->다음;
        노드_해제(p->타입);
        free(p->이름);
        free(p);
        p = 다음;
    }
}

static void 인자_해제(인자 *a) {
    while (a) {
        인자 *다음 = a->다음;
        노드_해제(a->값);
        free(a);
        a = 다음;
    }
}

static void 필드_해제(묶음필드 *f) {
    while (f) {
        묶음필드 *다음 = f->다음;
        노드_해제(f->타입);
        free(f->이름);
        free(f);
        f = 다음;
    }
}

static void 파이프단계_해제(파이프단계 *s) {
    while (s) {
        파이프단계 *다음 = s->다음;
        free(s->동사);
        인자_해제(s->인자목록);
        free(s);
        s = 다음;
    }
}

void 노드_해제(노드 *n) {
    if (!n) return;

    switch (n->종류) {
    case 노드_프로그램:
        for (int i = 0; i < n->데이터.프로그램.선언_수; i++)
            노드_해제(n->데이터.프로그램.선언목록[i]);
        free(n->데이터.프로그램.선언목록);
        break;

    case 노드_함수정의:
        free(n->데이터.함수정의.이름);
        매개변수_해제(n->데이터.함수정의.매개변수목록);
        노드_해제(n->데이터.함수정의.반환타입);
        노드_해제(n->데이터.함수정의.본문);
        break;

    case 노드_변수선언:
        free(n->데이터.변수선언.이름);
        노드_해제(n->데이터.변수선언.타입);
        노드_해제(n->데이터.변수선언.초기값);
        break;

    case 노드_묶음정의:
        free(n->데이터.묶음정의.이름);
        필드_해제(n->데이터.묶음정의.필드목록);
        break;

    case 노드_합침정의:
        free(n->데이터.합침정의.이름);
        필드_해제(n->데이터.합침정의.필드목록);
        break;

    case 노드_매크로정의:
        free(n->데이터.매크로정의.이름);
        free(n->데이터.매크로정의.값);
        break;

    case 노드_나열정의:
        free(n->데이터.나열정의.이름);
        for (int i = 0; i < n->데이터.나열정의.값_수; i++)
            free(n->데이터.나열정의.값이름목록[i]);
        free(n->데이터.나열정의.값이름목록);
        free(n->데이터.나열정의.값목록);
        break;

    case 노드_블록:
        for (int i = 0; i < n->데이터.블록.문장_수; i++)
            노드_해제(n->데이터.블록.문장목록[i]);
        free(n->데이터.블록.문장목록);
        break;

    case 노드_조건문:
        노드_해제(n->데이터.조건문.조건);
        노드_해제(n->데이터.조건문.참블록);
        노드_해제(n->데이터.조건문.거짓블록);
        break;

    case 노드_동안반복:
        노드_해제(n->데이터.동안반복.조건);
        노드_해제(n->데이터.동안반복.본문);
        break;

    case 노드_범위반복:
        free(n->데이터.범위반복.변수이름);
        노드_해제(n->데이터.범위반복.시작값);
        노드_해제(n->데이터.범위반복.끝값);
        노드_해제(n->데이터.범위반복.본문);
        break;

    case 노드_반환문:
        노드_해제(n->데이터.반환문.값);
        break;

    case 노드_이항연산:
        노드_해제(n->데이터.이항연산.왼쪽);
        노드_해제(n->데이터.이항연산.오른쪽);
        break;

    case 노드_단항연산:
        노드_해제(n->데이터.단항연산.피연산자);
        break;

    case 노드_호출:
        free(n->데이터.호출.함수이름);
        인자_해제(n->데이터.호출.인자목록);
        break;

    case 노드_멤버접근:
        노드_해제(n->데이터.멤버접근.대상);
        free(n->데이터.멤버접근.멤버이름);
        break;

    case 노드_배열접근:
        노드_해제(n->데이터.배열접근.배열);
        노드_해제(n->데이터.배열접근.인덱스);
        break;

    case 노드_대입:
        노드_해제(n->데이터.대입.대상);
        노드_해제(n->데이터.대입.값);
        break;

    case 노드_형변환:
        노드_해제(n->데이터.형변환.값);
        노드_해제(n->데이터.형변환.대상타입);
        break;

    case 노드_주소참조:
    case 노드_역참조:
        노드_해제(n->데이터.참조연산.대상);
        break;

    case 노드_크기:
        노드_해제(n->데이터.크기연산.대상);
        break;

    case 노드_파이프라인:
        노드_해제(n->데이터.파이프라인.시작값);
        파이프단계_해제(n->데이터.파이프라인.단계목록);
        break;

    case 노드_표현식문:
        노드_해제(n->데이터.표현식문.표현식);
        break;

    case 노드_참조타입:
        노드_해제(n->데이터.참조타입.대상타입);
        break;

    case 노드_배열타입:
        노드_해제(n->데이터.배열타입.원소타입);
        break;

    case 노드_식별자:
        free(n->데이터.식별자.이름);
        break;

    case 노드_문자열_리터럴:
        free(n->데이터.문자열_리터럴.값);
        break;

    case 노드_묶음타입명:
        free(n->데이터.묶음타입명.이름);
        break;

    case 노드_함수포인터타입:
        for (int i = 0; i < n->데이터.함수포인터타입.매개변수타입_수; i++)
            노드_해제(n->데이터.함수포인터타입.매개변수타입들[i]);
        free(n->데이터.함수포인터타입.매개변수타입들);
        노드_해제(n->데이터.함수포인터타입.반환타입);
        break;

    case 노드_정수_리터럴:
    case 노드_실수_리터럴:
    case 노드_문자_리터럴:
    case 노드_참거짓_리터럴:
    case 노드_없음_리터럴:
    case 노드_기본타입:
    case 노드_탈출문:
    case 노드_계속문:
        break; /* 해제할 동적 메모리 없음 */
    }

    free(n);
}

/* ===== 디버깅용 AST 출력 ===== */

static void 들여쓰기_출력(int 깊이) {
    for (int i = 0; i < 깊이; i++) printf("  ");
}

static const char *역할_이름(조사_역할 역할) {
    switch (역할) {
    case 역할_없음:   return "";
    case 역할_목적어: return "을/를";
    case 역할_주어:   return "이/가";
    case 역할_주제:   return "은/는";
    case 역할_목적지: return "에";
    case 역할_수단:   return "로";
    case 역할_출처:   return "에서";
    case 역할_시작:   return "부터";
    case 역할_종점:   return "까지";
    case 역할_공동:   return "와/과";
    case 역할_소유:   return "의";
    case 역할_비교:   return "보다";
    case 역할_대상:   return "에게";
    }
    return "?";
}

void ast_출력(노드 *n, int 깊이) {
    if (!n) return;

    들여쓰기_출력(깊이);

    switch (n->종류) {
    case 노드_프로그램:
        printf("프로그램 (선언 %d개)\n", n->데이터.프로그램.선언_수);
        for (int i = 0; i < n->데이터.프로그램.선언_수; i++)
            ast_출력(n->데이터.프로그램.선언목록[i], 깊이 + 1);
        break;

    case 노드_함수정의:
        printf("함수정의: %s\n", n->데이터.함수정의.이름);
        {
            매개변수 *p = n->데이터.함수정의.매개변수목록;
            while (p) {
                들여쓰기_출력(깊이 + 1);
                printf("매개변수: %s [%s]\n", p->이름, 역할_이름(p->역할));
                p = p->다음;
            }
        }
        if (n->데이터.함수정의.본문)
            ast_출력(n->데이터.함수정의.본문, 깊이 + 1);
        break;

    case 노드_변수선언:
        printf("변수선언: %s\n", n->데이터.변수선언.이름);
        if (n->데이터.변수선언.초기값)
            ast_출력(n->데이터.변수선언.초기값, 깊이 + 1);
        break;

    case 노드_블록:
        printf("블록 (문장 %d개)\n", n->데이터.블록.문장_수);
        for (int i = 0; i < n->데이터.블록.문장_수; i++)
            ast_출력(n->데이터.블록.문장목록[i], 깊이 + 1);
        break;

    case 노드_조건문:
        printf("조건문 (-면)\n");
        들여쓰기_출력(깊이 + 1); printf("조건:\n");
        ast_출력(n->데이터.조건문.조건, 깊이 + 2);
        들여쓰기_출력(깊이 + 1); printf("참:\n");
        ast_출력(n->데이터.조건문.참블록, 깊이 + 2);
        if (n->데이터.조건문.거짓블록) {
            들여쓰기_출력(깊이 + 1); printf("거짓:\n");
            ast_출력(n->데이터.조건문.거짓블록, 깊이 + 2);
        }
        break;

    case 노드_동안반복:
        printf("동안반복\n");
        ast_출력(n->데이터.동안반복.조건, 깊이 + 1);
        ast_출력(n->데이터.동안반복.본문, 깊이 + 1);
        break;

    case 노드_범위반복:
        printf("범위반복: %s\n", n->데이터.범위반복.변수이름);
        들여쓰기_출력(깊이 + 1); printf("시작:\n");
        ast_출력(n->데이터.범위반복.시작값, 깊이 + 2);
        들여쓰기_출력(깊이 + 1); printf("끝:\n");
        ast_출력(n->데이터.범위반복.끝값, 깊이 + 2);
        ast_출력(n->데이터.범위반복.본문, 깊이 + 1);
        break;

    case 노드_반환문:
        printf("반환\n");
        if (n->데이터.반환문.값)
            ast_출력(n->데이터.반환문.값, 깊이 + 1);
        break;

    case 노드_정수_리터럴:
        printf("정수: %lld\n", n->데이터.정수_리터럴.값);
        break;

    case 노드_실수_리터럴:
        printf("실수: %f\n", n->데이터.실수_리터럴.값);
        break;

    case 노드_문자열_리터럴:
        printf("문자열: %s\n", n->데이터.문자열_리터럴.값);
        break;

    case 노드_식별자:
        printf("식별자: %s\n", n->데이터.식별자.이름);
        break;

    case 노드_이항연산:
        printf("이항연산: %s\n", 토큰_이름(n->데이터.이항연산.연산자));
        ast_출력(n->데이터.이항연산.왼쪽, 깊이 + 1);
        ast_출력(n->데이터.이항연산.오른쪽, 깊이 + 1);
        break;

    case 노드_호출:
        printf("호출: %s\n", n->데이터.호출.함수이름);
        {
            인자 *a = n->데이터.호출.인자목록;
            while (a) {
                들여쓰기_출력(깊이 + 1);
                printf("인자 [%s]:\n", 역할_이름(a->역할));
                ast_출력(a->값, 깊이 + 2);
                a = a->다음;
            }
        }
        break;

    case 노드_파이프라인:
        printf("파이프라인\n");
        들여쓰기_출력(깊이 + 1); printf("시작:\n");
        ast_출력(n->데이터.파이프라인.시작값, 깊이 + 2);
        {
            파이프단계 *s = n->데이터.파이프라인.단계목록;
            while (s) {
                들여쓰기_출력(깊이 + 1);
                printf("→ %s\n", s->동사);
                s = s->다음;
            }
        }
        break;

    case 노드_합침정의:
        printf("합침정의: %s\n", n->데이터.합침정의.이름);
        {
            묶음필드 *f = n->데이터.합침정의.필드목록;
            while (f) {
                들여쓰기_출력(깊이 + 1);
                printf("필드: %s\n", f->이름);
                f = f->다음;
            }
        }
        break;

    case 노드_매크로정의:
        printf("매크로정의: %s = %s\n", n->데이터.매크로정의.이름,
               n->데이터.매크로정의.값 ? n->데이터.매크로정의.값 : "(없음)");
        break;

    case 노드_함수포인터타입:
        printf("함수포인터타입 (매개변수 %d개)\n",
               n->데이터.함수포인터타입.매개변수타입_수);
        for (int i = 0; i < n->데이터.함수포인터타입.매개변수타입_수; i++)
            ast_출력(n->데이터.함수포인터타입.매개변수타입들[i], 깊이 + 1);
        들여쓰기_출력(깊이 + 1); printf("→ 반환:\n");
        ast_출력(n->데이터.함수포인터타입.반환타입, 깊이 + 2);
        break;

    default:
        printf("노드(%d)\n", n->종류);
        break;
    }
}
