/* ===== 내부 유틸리티 ===== */

static void 파서_오류(구문분석기 *파서, const char *형식, ...) {
    if (파서->패닉_모드) return;
    파서->패닉_모드 = true;

    if (파서->오류_수 < 64) {
        char *buf = 파서->오류목록[파서->오류_수];
        int offset = snprintf(buf, 256, "%s:%d:%d: 오류: ",
                             파서->현재.위치.파일명,
                             파서->현재.위치.줄,
                             파서->현재.위치.열);
        va_list args;
        va_start(args, 형식);
        vsnprintf(buf + offset, 256 - offset, 형식, args);
        va_end(args);
        파서->오류_수++;
    }
}

/* 다음 토큰 읽기 (줄바꿈은 건너뜀) */
static void 전진(구문분석기 *파서) {
    파서->이전 = 파서->현재;
    do {
        파서->현재 = 다음_토큰(파서->분석기);
    } while (파서->현재.종류 == 토큰_줄바꿈);
}

/* 현재 토큰이 특정 종류인지 확인 */
static bool 확인(구문분석기 *파서, 토큰_종류 종류) {
    return 파서->현재.종류 == 종류;
}

/* 특정 종류의 토큰이면 소비하고 true, 아니면 false */
static bool 일치(구문분석기 *파서, 토큰_종류 종류) {
    if (확인(파서, 종류)) {
        전진(파서);
        return true;
    }
    return false;
}

/* 특정 종류의 토큰을 기대 (없으면 오류) */
static void 기대(구문분석기 *파서, 토큰_종류 종류) {
    if (확인(파서, 종류)) {
        전진(파서);
        return;
    }
    파서_오류(파서, "'%s'이(가) 필요하지만 '%s'이(가) 있습니다",
             토큰_이름(종류), 토큰_이름(파서->현재.종류));
}

/* 조사 토큰이면 소비하고 역할 반환, 아니면 역할_없음 */
static 조사_역할 조사_소비(구문분석기 *파서) {
    if (조사_토큰인가(파서->현재.종류)) {
        조사_역할 역할 = 파서->현재.역할;
        전진(파서);
        return 역할;
    }
    return 역할_없음;
}

/* 어미 토큰이면 소비하고 기능 반환 */
static 어미_기능 어미_소비(구문분석기 *파서) {
    if (어미_토큰인가(파서->현재.종류)) {
        어미_기능 기능 = 파서->현재.기능;
        전진(파서);
        return 기능;
    }
    return 어미_없음;
}

/* 오류 복구: 다음 문장 경계까지 건너뛰기 */
static void 동기화(구문분석기 *파서) {
    파서->패닉_모드 = false;

    while (!확인(파서, 토큰_파일끝)) {
        if (확인(파서, 토큰_마침표)) { 전진(파서); return; }
        if (확인(파서, 토큰_닫는중괄호)) return;
        /* 닫는대괄호: 소비하고 반환 (블록 내에서 무한루프 방지) */
        if (확인(파서, 토큰_닫는대괄호)) { 전진(파서); return; }

        /* 새로운 선언의 시작 */
        switch (파서->현재.종류) {
        case 토큰_정수: case 토큰_실수: case 토큰_문자:
        case 토큰_문자열: case 토큰_참거짓: case 토큰_공허:
        case 토큰_묶음: case 토큰_나열: case 토큰_합침:
        case 토큰_여는대괄호:
            return;
        default:
            전진(파서);
        }
    }
}

/* 타입인지 확인 */
static bool 묶음이름인가(구문분석기 *파서, const char *이름) {
    for (int i = 0; i < 파서->묶음이름_수; i++) {
        if (strcmp(파서->묶음이름목록[i], 이름) == 0) return true;
    }
    return false;
}

static void 묶음이름_등록(구문분석기 *파서, const char *이름) {
    if (파서->묶음이름_수 < 128 && !묶음이름인가(파서, 이름)) {
        파서->묶음이름목록[파서->묶음이름_수++] = strdup(이름);
    }
}

static bool 타입_시작인가(구문분석기 *파서) {
    switch (파서->현재.종류) {
    case 토큰_정수: case 토큰_긴정수: case 토큰_짧은정수:
    case 토큰_작은정수: case 토큰_부호없는:
    case 토큰_실수: case 토큰_짧은실수:
    case 토큰_문자: case 토큰_문자열:
    case 토큰_참거짓: case 토큰_공허:
    case 토큰_가변목록:
    case 토큰_여는대괄호:  /* 함수 포인터 타입: [타입 → 타입] */
        return true;
    default:
        /* 등록된 묶음(struct) 이름인지 확인 */
        if (파서->현재.종류 == 토큰_식별자 &&
            묶음이름인가(파서, 파서->현재.텍스트))
            return true;
        return false;
    }
}

/* SOV 호출 패턴을 유발하는 조사인지 확인 (은/는/의 제외) */
static bool SOV_조사인가(토큰_종류 종류) {
    switch (종류) {
    case 토큰_조사_을: case 토큰_조사_를:
    case 토큰_조사_이: case 토큰_조사_가:
    case 토큰_조사_에:
    case 토큰_조사_로: case 토큰_조사_으로:
    case 토큰_조사_에서:
    case 토큰_조사_부터: case 토큰_조사_까지:
    case 토큰_조사_보다:
    case 토큰_조사_와: case 토큰_조사_과:
    case 토큰_조사_에게:
        return true;
    default:
        return false;
    }
}

