/* ===== 타입 파싱 ===== */

static 노드 *타입_파싱(구문분석기 *파서) {
    소스위치 위치 = 파서->현재.위치;

    /* 함수 포인터 타입: [타입, 타입 → 반환타입] */
    if (확인(파서, 토큰_여는대괄호)) {
        전진(파서); /* '[' 소비 */

        /* 매개변수 타입 목록 파싱 */
        노드 *매개변수_임시[64];
        int 매개변수_수 = 0;

        /* 빈 매개변수 목록 [→ 반환타입] 허용 */
        if (!확인(파서, 토큰_화살표)) {
            매개변수_임시[매개변수_수++] = 타입_파싱(파서);
            while (확인(파서, 토큰_쉼표)) {
                전진(파서); /* ',' 소비 */
                if (매개변수_수 >= 64) {
                    파서_오류(파서, "함수 포인터 매개변수가 너무 많습니다");
                    return NULL;
                }
                매개변수_임시[매개변수_수++] = 타입_파싱(파서);
            }
        }

        /* → (화살표) */
        기대(파서, 토큰_화살표);

        /* 반환 타입 */
        노드 *반환 = 타입_파싱(파서);

        /* ']' */
        기대(파서, 토큰_닫는대괄호);

        노드 *n = 노드_생성(노드_함수포인터타입, 위치);
        n->데이터.함수포인터타입.매개변수타입_수 = 매개변수_수;
        n->데이터.함수포인터타입.반환타입 = 반환;
        if (매개변수_수 > 0) {
            n->데이터.함수포인터타입.매개변수타입들 =
                (노드 **)malloc(sizeof(노드 *) * 매개변수_수);
            memcpy(n->데이터.함수포인터타입.매개변수타입들,
                   매개변수_임시, sizeof(노드 *) * 매개변수_수);
        } else {
            n->데이터.함수포인터타입.매개변수타입들 = NULL;
        }
        return n;
    }

    /* 묶음(struct) 타입 이름 확인 (기본타입보다 먼저) */
    if (파서->현재.종류 == 토큰_식별자 && 묶음이름인가(파서, 파서->현재.텍스트)) {
        노드 *n = 노드_생성(노드_묶음타입명, 위치);
        n->데이터.묶음타입명.이름 = 문자열_복사(파서->현재.텍스트);
        전진(파서);

        /* 참조(포인터) */
        while (확인(파서, 토큰_참조)) {
            전진(파서);
            노드 *참조 = 노드_생성(노드_참조타입, 위치);
            참조->데이터.참조타입.대상타입 = n;
            n = 참조;
        }
        return n;
    }

    if (!타입_시작인가(파서)) {
        /* 등록되지 않은 식별자도 묶음 타입일 수 있음 */
        if (확인(파서, 토큰_식별자)) {
            노드 *n = 노드_생성(노드_묶음타입명, 위치);
            n->데이터.묶음타입명.이름 = 문자열_복사(파서->현재.텍스트);
            전진(파서);
            return n;
        }
        파서_오류(파서, "타입이 필요합니다");
        return NULL;
    }

    노드 *n = 노드_생성(노드_기본타입, 위치);
    n->데이터.기본타입.기본 = 파서->현재.종류;
    전진(파서);

    /* 참조(포인터): 타입 뒤에 "참조" (다중 가능: 참조 참조 → **) */
    while (확인(파서, 토큰_참조)) {
        전진(파서);
        노드 *참조 = 노드_생성(노드_참조타입, 위치);
        참조->데이터.참조타입.대상타입 = n;
        n = 참조;
    }

    /* 배열: 타입[크기] */
    if (확인(파서, 토큰_여는대괄호)) {
        전진(파서);
        int 크기 = 0;
        if (확인(파서, 토큰_정수_리터럴)) {
            크기 = (int)파서->현재.값.정수값;
            전진(파서);
        }
        기대(파서, 토큰_닫는대괄호);
        노드 *배열 = 노드_생성(노드_배열타입, 위치);
        배열->데이터.배열타입.원소타입 = n;
        배열->데이터.배열타입.크기 = 크기;
        n = 배열;
    }

    return n;
}

