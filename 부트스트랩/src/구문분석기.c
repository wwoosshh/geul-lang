/* 우선순위 */
typedef enum {
    우선_없음,
    우선_대입,       /* = */
    우선_논리합,     /* || */
    우선_논리곱,     /* && */
    우선_비트합,     /* | */
    우선_비트배타,   /* ^ */
    우선_비트곱,     /* & */
    우선_동등,       /* == != */
    우선_비교,       /* < > <= >= 보다 */
    우선_이동,       /* << >> */
    우선_덧셈,       /* + - */
    우선_곱셈,       /* * / % */
    우선_단항,       /* ! ~ - */
    우선_호출,       /* 함수호출, 배열접근, 멤버접근 */
} 우선순위;

static 우선순위 이항_우선순위(토큰_종류 종류) {
    switch (종류) {
    case 토큰_또는:        return 우선_논리합;
    case 토큰_그리고:      return 우선_논리곱;
    case 토큰_비트또는:    return 우선_비트합;
    case 토큰_비트배타:    return 우선_비트배타;
    case 토큰_비트와:      return 우선_비트곱;
    case 토큰_같다:
    case 토큰_다르다:      return 우선_동등;
    case 토큰_크다:
    case 토큰_작다:
    case 토큰_크거나같다:
    case 토큰_작거나같다:  return 우선_비교;
    case 토큰_왼쪽이동:
    case 토큰_오른쪽이동:  return 우선_이동;
    case 토큰_더하기:
    case 토큰_빼기:        return 우선_덧셈;
    case 토큰_곱하기:
    case 토큰_나누기:
    case 토큰_나머지:      return 우선_곱셈;
    default:               return 우선_없음;
    }
}

/* 기본 표현식 (원자) */
static 노드 *기본_표현식(구문분석기 *파서) {
    소스위치 위치 = 파서->현재.위치;

    /* 정수 리터럴 */
    if (확인(파서, 토큰_정수_리터럴)) {
        노드 *n = 노드_생성(노드_정수_리터럴, 위치);
        n->데이터.정수_리터럴.값 = 파서->현재.값.정수값;
        전진(파서);
        return n;
    }

    /* 실수 리터럴 */
    if (확인(파서, 토큰_실수_리터럴)) {
        노드 *n = 노드_생성(노드_실수_리터럴, 위치);
        n->데이터.실수_리터럴.값 = 파서->현재.값.실수값;
        전진(파서);
        return n;
    }

    /* 문자열 리터럴 */
    if (확인(파서, 토큰_문자열_리터럴)) {
        노드 *n = 노드_생성(노드_문자열_리터럴, 위치);
        /* 따옴표 제거 + 이스케이프 처리 */
        size_t 길이 = 파서->현재.텍스트_길이;
        const char *원본;
        size_t 원본_길이;
        if (길이 >= 2 && 파서->현재.텍스트[0] == '"') {
            원본 = 파서->현재.텍스트 + 1;
            원본_길이 = 길이 - 2;
        } else {
            원본 = 파서->현재.텍스트;
            원본_길이 = 길이;
        }
        /* 이스케이프 시퀀스 처리 */
        char *결과 = (char *)malloc(원본_길이 + 1);
        size_t j = 0;
        for (size_t i = 0; i < 원본_길이; i++) {
            if (원본[i] == '\\' && i + 1 < 원본_길이) {
                switch (원본[i + 1]) {
                case 'n': 결과[j++] = '\n'; i++; break;
                case 't': 결과[j++] = '\t'; i++; break;
                case 'r': 결과[j++] = '\r'; i++; break;
                case '0': 결과[j++] = '\0'; i++; break;
                case '\\': 결과[j++] = '\\'; i++; break;
                case '"': 결과[j++] = '"'; i++; break;
                default: 결과[j++] = 원본[i]; break;
                }
            } else {
                결과[j++] = 원본[i];
            }
        }
        결과[j] = '\0';
        n->데이터.문자열_리터럴.값 = 결과;
        전진(파서);
        return n;
    }

    /* 참/거짓 */
    if (확인(파서, 토큰_참)) {
        노드 *n = 노드_생성(노드_참거짓_리터럴, 위치);
        n->데이터.참거짓_리터럴.값 = true;
        전진(파서);
        return n;
    }
    if (확인(파서, 토큰_거짓)) {
        노드 *n = 노드_생성(노드_참거짓_리터럴, 위치);
        n->데이터.참거짓_리터럴.값 = false;
        전진(파서);
        return n;
    }

    /* 없음 (NULL) */
    if (확인(파서, 토큰_없음)) {
        전진(파서);
        return 노드_생성(노드_없음_리터럴, 위치);
    }

    /* 묶음(struct) 리터럴 또는 배열 리터럴: { ... } */
    if (확인(파서, 토큰_여는중괄호)) {
        /* 미리보기로 구조체(이름:값) vs 배열(값,값) 구분 */
        토큰 미리 = 미리보기(파서->분석기);
        bool 구조체_리터럴 = false;

        /* { } 빈 리터럴은 배열로 처리 */
        /* { 이름: → 구조체, 그 외 → 배열 */
        if (이름_가능_종류(미리.종류)) {
            /* 두 번째 미리보기가 필요하지만, 간단히:
             * 이름 가능 토큰 뒤에 쌍점이면 구조체 */
            구조체_리터럴 = true;
            /* 실제로는 이름 뒤 쌍점 확인이 필요하지만,
             * 배열 원소가 식별자로 시작할 수도 있으므로
             * 첫 원소 파싱 후 쌍점 유무로 결정 */
        }

        전진(파서); /* { 소비 */

        if (확인(파서, 토큰_닫는중괄호)) {
            /* 빈 배열/구조체 리터럴 */
            기대(파서, 토큰_닫는중괄호);
            노드 *n = 노드_생성(노드_배열_리터럴, 위치);
            n->데이터.배열_리터럴.원소목록 = NULL;
            n->데이터.배열_리터럴.원소_수 = 0;
            return n;
        }

        /* 첫 번째 원소로 종류 판별 */
        if (구조체_리터럴 && 이름_가능(파서)) {
            /* 이름 후보 저장, 쌍점 확인 */
            char *이름후보 = 문자열_복사(파서->현재.텍스트);
            소스위치 이름위치 = 파서->현재.위치;
            전진(파서);
            if (확인(파서, 토큰_쌍점)) {
                /* 구조체 리터럴 확정 */
                전진(파서); /* : 소비 */
                int 용량 = 8;
                char **이름목록 = malloc(sizeof(char*) * 용량);
                노드 **값목록 = malloc(sizeof(노드*) * 용량);
                이름목록[0] = 이름후보;
                값목록[0] = 표현식_파싱(파서);
                int 필드수 = 1;
                while (확인(파서, 토큰_쉼표)) {
                    일치(파서, 토큰_쉼표);
                    if (필드수 >= 용량) {
                        용량 *= 2;
                        이름목록 = realloc(이름목록, sizeof(char*) * 용량);
                        값목록 = realloc(값목록, sizeof(노드*) * 용량);
                    }
                    if (!이름_가능(파서)) {
                        파서_오류(파서, "묶음 필드 이름이 필요합니다");
                        break;
                    }
                    이름목록[필드수] = 문자열_복사(파서->현재.텍스트);
                    전진(파서);
                    일치(파서, 토큰_쌍점);
                    값목록[필드수] = 표현식_파싱(파서);
                    필드수++;
                }
                기대(파서, 토큰_닫는중괄호);
                노드 *n = 노드_생성(노드_묶음_리터럴, 위치);
                n->데이터.묶음_리터럴.필드이름목록 = 이름목록;
                n->데이터.묶음_리터럴.필드값목록 = 값목록;
                n->데이터.묶음_리터럴.필드_수 = 필드수;
                return n;
            }
            /* 쌍점 없음 → 배열 리터럴이었음, 이름후보는 식별자 표현식 */
            /* 이름후보를 식별자 노드로 변환하여 첫 원소로 사용 */
            노드 *첫원소 = 노드_생성(노드_식별자, 이름위치);
            첫원소->데이터.식별자.이름 = 이름후보;
            /* 이후 배열 리터럴로 계속 */
            int 용량 = 8;
            노드 **원소목록 = malloc(sizeof(노드*) * 용량);
            원소목록[0] = 첫원소;
            int 원소수 = 1;
            while (확인(파서, 토큰_쉼표)) {
                일치(파서, 토큰_쉼표);
                if (원소수 >= 용량) { 용량 *= 2; 원소목록 = realloc(원소목록, sizeof(노드*) * 용량); }
                원소목록[원소수++] = 표현식_파싱(파서);
            }
            기대(파서, 토큰_닫는중괄호);
            노드 *n = 노드_생성(노드_배열_리터럴, 위치);
            n->데이터.배열_리터럴.원소목록 = 원소목록;
            n->데이터.배열_리터럴.원소_수 = 원소수;
            return n;
        }

        /* 배열 리터럴: { 값, 값, ... } */
        {
            int 용량 = 8;
            노드 **원소목록 = malloc(sizeof(노드*) * 용량);
            원소목록[0] = 표현식_파싱(파서);
            int 원소수 = 1;
            while (확인(파서, 토큰_쉼표)) {
                일치(파서, 토큰_쉼표);
                if (원소수 >= 용량) { 용량 *= 2; 원소목록 = realloc(원소목록, sizeof(노드*) * 용량); }
                원소목록[원소수++] = 표현식_파싱(파서);
            }
            기대(파서, 토큰_닫는중괄호);
            노드 *n = 노드_생성(노드_배열_리터럴, 위치);
            n->데이터.배열_리터럴.원소목록 = 원소목록;
            n->데이터.배열_리터럴.원소_수 = 원소수;
            return n;
        }
    }

    /* 괄호 그룹 */
    if (확인(파서, 토큰_여는괄호)) {
        전진(파서);
        노드 *n = 표현식_파싱(파서);
        기대(파서, 토큰_닫는괄호);
        return n;
    }

    /* 단항 연산자 */
    if (확인(파서, 토큰_아닌) || 확인(파서, 토큰_빼기) || 확인(파서, 토큰_비트아닌)) {
        토큰_종류 연산자 = 파서->현재.종류;
        전진(파서);
        노드 *n = 노드_생성(노드_단항연산, 위치);
        n->데이터.단항연산.연산자 = 연산자;
        n->데이터.단항연산.피연산자 = 기본_표현식(파서);
        return n;
    }

    /* 전위 증감: ++x, --x */
    if (확인(파서, 토큰_증가) || 확인(파서, 토큰_감소)) {
        bool 증가 = 파서->현재.종류 == 토큰_증가;
        전진(파서);
        노드 *n = 노드_생성(노드_증감, 위치);
        n->데이터.증감.대상 = 기본_표현식(파서);
        n->데이터.증감.증가 = 증가;
        n->데이터.증감.전위 = true;
        return n;
    }

    /* 주소참조: &식별자 */
    if (확인(파서, 토큰_비트와)) {
        전진(파서);
        노드 *n = 노드_생성(노드_주소참조, 위치);
        n->데이터.참조연산.대상 = 기본_표현식(파서);
        return n;
    }

    /* 역참조: *표현식 */
    if (확인(파서, 토큰_곱하기)) {
        전진(파서);
        노드 *n = 노드_생성(노드_역참조, 위치);
        n->데이터.참조연산.대상 = 기본_표현식(파서);
        return n;
    }

    /* 크기 연산: 크기(타입) — 단, 직후에 '('가 올 때만 sizeof로 처리 */
    /* 가변인자(ap, 타입) → va_arg */
    if (확인(파서, 토큰_가변인자)) {
        전진(파서);
        일치(파서, 토큰_여는괄호);
        노드 *n = 노드_생성(노드_가변인자, 위치);
        if (!이름_가능(파서)) {
            파서_오류(파서, "가변목록 변수 이름이 필요합니다");
            return NULL;
        }
        n->데이터.가변인자.목록이름 = 문자열_복사(파서->현재.텍스트);
        전진(파서);
        일치(파서, 토큰_쉼표);
        n->데이터.가변인자.타입 = 타입_파싱(파서);
        일치(파서, 토큰_닫는괄호);
        return n;
    }

    if (확인(파서, 토큰_크기)) {
        /* 렉서 버퍼에 토큰이 있으면 다음 토큰을 미리보기 */
        토큰 다음 = 미리보기(파서->분석기);
        /* 줄바꿈을 건너뛰고 실제 다음 토큰 확인 */
        while (다음.종류 == 토큰_줄바꿈) {
            /* 미리보기로는 건너뛸 수 없으므로, 버퍼에서 직접 확인 */
            break; /* 간단히: 줄바꿈이면 sizeof가 아님 */
        }
        if (다음.종류 == 토큰_여는괄호) {
            전진(파서);
            기대(파서, 토큰_여는괄호);
            노드 *n = 노드_생성(노드_크기, 위치);
            if (타입_시작인가(파서)) {
                n->데이터.크기연산.대상 = 타입_파싱(파서);
            } else {
                n->데이터.크기연산.대상 = 표현식_파싱(파서);
            }
            기대(파서, 토큰_닫는괄호);
            return n;
        }
        /* '(' 없으면 식별자 (변수명 '크기')로 처리 — 아래 식별자 섹션으로 */
        goto 식별자_처리;
    }

    /* 식별자 */
    식별자_처리:
    if (확인(파서, 토큰_식별자) || 확인(파서, 토큰_크기)) {
        노드 *n = 노드_생성(노드_식별자, 위치);
        n->데이터.식별자.이름 = 문자열_복사(파서->현재.텍스트);
        전진(파서);

        /* 배열 접근: 식별자[인덱스] */
        if (확인(파서, 토큰_여는대괄호)) {
            전진(파서);
            노드 *배열 = 노드_생성(노드_배열접근, 위치);
            배열->데이터.배열접근.배열 = n;
            배열->데이터.배열접근.인덱스 = 표현식_파싱(파서);
            기대(파서, 토큰_닫는대괄호);
            n = 배열;
        }

        return n;
    }

    /*
     * 조사/어미 토큰이 식별자 위치에 온 경우:
     * 단일 음절 변수명(가, 나, 이 등)이 조사/어미와 겹칠 때
     * 표현식의 시작에서 조사/어미는 올 수 없으므로 식별자로 처리
     */
    if (조사_토큰인가(파서->현재.종류) || 어미_토큰인가(파서->현재.종류)) {
        /* 단, 문장 종결 어미(다)나 제어 어미(면, 동안)가 단독으로 오면 식별자가 아님 */
        /* 여기서는 값이 기대되는 위치이므로 식별자로 처리 */
        노드 *n = 노드_생성(노드_식별자, 위치);
        n->데이터.식별자.이름 = 문자열_복사(파서->현재.텍스트);
        전진(파서);
        return n;
    }

    파서_오류(파서, "표현식이 필요합니다 (현재: '%s')", 토큰_이름(파서->현재.종류));
    return 노드_생성(노드_없음_리터럴, 위치); /* 오류 복구용 더미 */
}

/* 접미 표현식: 멤버접근(의), 배열접근 */
static 노드 *접미_표현식(구문분석기 *파서) {
    노드 *n = 기본_표현식(파서);

    while (true) {
        /* 멤버접근: "의" 조사 */
        if (확인(파서, 토큰_조사_의)) {
            전진(파서);
            소스위치 위치 = 파서->현재.위치;
            if (!확인(파서, 토큰_식별자)) {
                파서_오류(파서, "'의' 뒤에 멤버 이름이 필요합니다");
                break;
            }
            노드 *멤버 = 노드_생성(노드_멤버접근, 위치);
            멤버->데이터.멤버접근.대상 = n;
            멤버->데이터.멤버접근.멤버이름 = 문자열_복사(파서->현재.텍스트);
            전진(파서);
            n = 멤버;
            continue;
        }
        /* 포인터 멤버접근: "→" 화살표 */
        if (확인(파서, 토큰_화살표)) {
            전진(파서);
            소스위치 위치 = 파서->현재.위치;
            if (!이름_가능(파서)) {
                파서_오류(파서, "'→' 뒤에 멤버 이름이 필요합니다");
                break;
            }
            노드 *멤버 = 노드_생성(노드_멤버접근, 위치);
            멤버->데이터.멤버접근.대상 = n;
            멤버->데이터.멤버접근.멤버이름 = 문자열_복사(파서->현재.텍스트);
            멤버->데이터.멤버접근.포인터 = true;
            전진(파서);
            n = 멤버;
            continue;
        }
        /* 직접 함수 호출: 함수이름() 또는 함수포인터변수() */
        if (확인(파서, 토큰_여는괄호) && n->종류 == 노드_식별자) {
            전진(파서); /* ( 소비 */
            소스위치 위치 = n->위치;
            노드 *호출 = 노드_생성(노드_호출, 위치);
            호출->데이터.호출.함수이름 = 문자열_복사(n->데이터.식별자.이름);
            호출->데이터.호출.인자목록 = NULL;
            /* 인자가 있으면 파싱 */
            if (!확인(파서, 토큰_닫는괄호)) {
                인자 *인자_머리 = NULL;
                인자 *인자_꼬리 = NULL;
                do {
                    노드 *값 = 표현식_파싱(파서);
                    인자 *a = 인자_생성(값, 역할_없음);
                    if (!인자_머리) { 인자_머리 = 인자_꼬리 = a; }
                    else { 인자_꼬리->다음 = a; 인자_꼬리 = a; }
                } while (확인(파서, 토큰_쉼표) && (전진(파서), true));
                호출->데이터.호출.인자목록 = 인자_머리;
            }
            일치(파서, 토큰_닫는괄호);
            노드_해제(n);
            n = 호출;
            continue;
        }

        /* 후위 증감: x++, x-- */
        if (확인(파서, 토큰_증가) || 확인(파서, 토큰_감소)) {
            bool 증가 = 파서->현재.종류 == 토큰_증가;
            소스위치 위치 = 파서->현재.위치;
            전진(파서);
            노드 *증감노드 = 노드_생성(노드_증감, 위치);
            증감노드->데이터.증감.대상 = n;
            증감노드->데이터.증감.증가 = 증가;
            증감노드->데이터.증감.전위 = false;
            n = 증감노드;
            continue;
        }
        break;
    }

    return n;
}

/* 이항 표현식 (프랫 파싱) */
static 노드 *이항_표현식(구문분석기 *파서, int 최소우선) {
    노드 *왼쪽 = 접미_표현식(파서);

    while (true) {
        우선순위 현재우선 = 이항_우선순위(파서->현재.종류);
        if ((int)현재우선 <= 최소우선) break;

        토큰_종류 연산자 = 파서->현재.종류;
        소스위치 위치 = 파서->현재.위치;
        전진(파서);

        노드 *오른쪽 = 이항_표현식(파서, (int)현재우선);

        노드 *n = 노드_생성(노드_이항연산, 위치);
        n->데이터.이항연산.연산자 = 연산자;
        n->데이터.이항연산.왼쪽 = 왼쪽;
        n->데이터.이항연산.오른쪽 = 오른쪽;
        왼쪽 = n;
    }

    return 왼쪽;
}

/*
 * SOV 호출 파싱:
 * 한글의 SOV(주어-목적어-동사) 어순을 처리한다.
 *
 * 패턴: 값1(조사1) [값2(조사2)]* 동사이름
 * 예: "안녕"를 출력       → 출력("안녕")
 *     5를 3에 더하기       → 더하기(5, 3)
 *     나이가 18보다 크      → 크(나이, 18)  (비교로 변환 가능)
 *
 * 첫 번째 값과 조사는 이미 파싱된 상태로 전달된다.
 * 동사 뒤의 어미는 소비하지 않는다 (호출자가 처리).
 */
static 노드 *SOV_호출_파싱(구문분석기 *파서, 노드 *첫값, 조사_역할 첫역할) {
    소스위치 위치 = 첫값->위치;

    /* 인자 링크드 리스트 구성 */
    인자 *인자_머리 = 인자_생성(첫값, 첫역할);
    인자 *인자_꼬리 = 인자_머리;

    while (true) {
        /* 형변환 패턴: 타입 + 로/으로 → (타입)값 */
        if (타입_시작인가(파서)) {
            노드 *타입 = 타입_파싱(파서);
            if (파서->현재.종류 == 토큰_조사_로 || 파서->현재.종류 == 토큰_조사_으로) {
                전진(파서); /* 로/으로 소비 */
                /* 첫 번째 인자를 형변환 대상으로 사용 */
                노드 *변환 = 노드_생성(노드_형변환, 위치);
                변환->데이터.형변환.값 = 인자_머리->값;
                변환->데이터.형변환.대상타입 = 타입;
                /* 후속 처리: 형변환 결과가 다른 SOV의 인자가 될 수 있음 */
                if (SOV_조사인가(파서->현재.종류)) {
                    조사_역할 역할 = 파서->현재.역할;
                    전진(파서);
                    return SOV_호출_파싱(파서, 변환, 역할);
                }
                return 변환;
            }
            /* 타입이었지만 로/으로가 아님 → 오류 */
            파서_오류(파서, "형변환에는 '로/으로'가 필요합니다");
            노드_해제(타입);
        }

        /* 다음 값 파싱 (이항 표현식까지, SOV 중첩은 하지 않음) */
        노드 *다음값 = 이항_표현식(파서, 우선_없음);

        /* 조사가 오면 → 이것은 인자 */
        /* 단, "이"+"다" 패턴은 SOV가 아니라 "이다" 서술격 조사 */
        if (SOV_조사인가(파서->현재.종류)) {
            /* "이"/"가" 뒤에 어미_다 또는 마침표가 오면 SOV가 아님 */
            if ((파서->현재.종류 == 토큰_조사_이 || 파서->현재.종류 == 토큰_조사_가)) {
                토큰 미리 = 미리보기(파서->분석기);
                if (미리.종류 == 토큰_어미_다 || 미리.종류 == 토큰_마침표) {
                    /* "이다" 패턴 → 동사 위치의 값을 반환 */
                    goto SOV_동사_처리;
                }
            }

            /* 다중 인자: 같은 조사가 반복되면 모두 인자로 수집
             * 중첩 호출이 필요하면 명시적 괄호 사용:
             * (번호를 피보나치다)를 출력하다 → 출력(피보나치(번호)) */
            조사_역할 역할 = 파서->현재.역할;
            전진(파서);

            인자 *새인자 = 인자_생성(다음값, 역할);
            인자_꼬리->다음 = 새인자;
            인자_꼬리 = 새인자;
            continue;
        }

        /* 조사가 없으면 → 이것이 동사(함수 이름) */
        SOV_동사_처리:
        if (다음값->종류 == 노드_식별자) {
            노드 *호출 = 노드_생성(노드_호출, 위치);
            호출->데이터.호출.함수이름 = 문자열_복사(다음값->데이터.식별자.이름);
            호출->데이터.호출.인자목록 = 인자_머리;
            노드_해제(다음값);
            return 호출;
        }

        /* 동사 위치에 식별자가 아닌 것이 온 경우 - 오류 복구 */
        /* 마지막 값을 무시하고 직전까지의 인자로 호출 생성 시도 */
        파서_오류(파서, "SOV 패턴에서 동사(함수이름)가 필요합니다");
        노드_해제(다음값);

        노드 *호출 = 노드_생성(노드_호출, 위치);
        호출->데이터.호출.함수이름 = 문자열_복사("?");
        호출->데이터.호출.인자목록 = 인자_머리;
        return 호출;
    }
}

/*
 * 표현식 파싱 (최상위)
 *
 * 이항 표현식을 먼저 파싱한 후,
 * SOV 조사가 오면 SOV 호출 패턴으로 전환한다.
 */
static 노드 *표현식_파싱(구문분석기 *파서) {
    노드 *값 = 이항_표현식(파서, 우선_없음);

    /* 삼항 연산자: 조건 ? 참값 : 거짓값 */
    if (확인(파서, 토큰_물음표)) {
        소스위치 위치 = 파서->현재.위치;
        전진(파서); /* ? 소비 */
        노드 *n = 노드_생성(노드_삼항, 위치);
        n->데이터.삼항.조건 = 값;
        n->데이터.삼항.참값 = 표현식_파싱(파서);
        일치(파서, 토큰_쌍점);
        n->데이터.삼항.거짓값 = 표현식_파싱(파서);
        return n;
    }

    /* SOV 호출 패턴 확인: 값 뒤에 조사가 오면 */
    if (SOV_조사인가(파서->현재.종류)) {
        /* "이"/"가" 뒤에 어미(면, 동안, 고, 서, 다)가 오면 SOV가 아님
         * 예: 파일 == 없음이면 → 파일 == 없음 + 이(소비) + 면(조건)
         * 조사 "이/가"는 서술격 연결사로 소비하고 표현식만 반환 */
        if (파서->현재.종류 == 토큰_조사_이 || 파서->현재.종류 == 토큰_조사_가) {
            토큰 미리 = 미리보기(파서->분석기);
            if (미리.종류 == 토큰_어미_면 || 미리.종류 == 토큰_어미_동안 ||
                미리.종류 == 토큰_어미_고 || 미리.종류 == 토큰_어미_서 ||
                미리.종류 == 토큰_어미_다 || 미리.종류 == 토큰_마침표) {
                전진(파서); /* 이/가 소비 */
                return 값;
            }
        }
        조사_역할 역할 = 파서->현재.역할;
        전진(파서);
        return SOV_호출_파싱(파서, 값, 역할);
    }

    return 값;
}

/* ===== 문장 파싱 ===== */

/* 변수 선언: "타입 이름은/는 값이다." 또는 "타입 이름은/는 값다." */
static 노드 *변수선언_파싱(구문분석기 *파서) {
    소스위치 위치 = 파서->현재.위치;

    노드 *타입 = 타입_파싱(파서);
    if (!타입) return NULL;

    /* 변수 이름: 식별자뿐 아니라 키워드도 변수명으로 허용 (크기, 반환 등) */
    if (!이름_가능(파서)) {
        파서_오류(파서, "변수 이름이 필요합니다");
        노드_해제(타입);
        return NULL;
    }

    노드 *n = 노드_생성(노드_변수선언, 위치);
    n->데이터.변수선언.타입 = 타입;
    n->데이터.변수선언.이름 = 문자열_복사(파서->현재.텍스트);
    전진(파서);

    /* "은/는" 뒤에 초기값 */
    if (확인(파서, 토큰_조사_은) || 확인(파서, 토큰_조사_는)) {
        전진(파서);
        n->데이터.변수선언.초기값 = 표현식_파싱(파서);
    }
    /* "=" 대입도 허용 */
    else if (확인(파서, 토큰_대입)) {
        전진(파서);
        n->데이터.변수선언.초기값 = 표현식_파싱(파서);
    }

    /* "이다" 패턴 처리: "18이다" → "이"(식별자) + "다"(어미)
     * 렉서가 "이다"를 식별자("이") + 어미_다로 분리하므로
     * 초기값 뒤에 남은 "이" 식별자를 건너뛴다 */
    if (확인(파서, 토큰_식별자) && strcmp(파서->현재.텍스트, "이") == 0) {
        전진(파서); /* "이" 소비 */
    }
    /* "이"가 조사로 토큰화된 경우도 처리 */
    else if (확인(파서, 토큰_조사_이)) {
        전진(파서);
    }

    /* 종결 어미: "다" */
    어미_소비(파서);
    일치(파서, 토큰_마침표);

    return n;
}

/* 간략 변수 선언 (for 루프 초기화용, 어미/마침표 없음) */
static 노드 *변수선언_파싱_간략(구문분석기 *파서) {
    소스위치 위치 = 파서->현재.위치;
    노드 *타입 = 타입_파싱(파서);
    if (!타입) return NULL;

    if (!이름_가능(파서)) {
        파서_오류(파서, "변수 이름이 필요합니다");
        노드_해제(타입);
        return NULL;
    }

    노드 *n = 노드_생성(노드_변수선언, 위치);
    n->데이터.변수선언.타입 = 타입;
    n->데이터.변수선언.이름 = 문자열_복사(파서->현재.텍스트);
    전진(파서);

    /* "=" 대입으로 초기값 */
    if (확인(파서, 토큰_대입)) {
        전진(파서);
        n->데이터.변수선언.초기값 = 표현식_파싱(파서);
    }

    return n;
}

/* 조건문: "조건표현식 -면 블록 아니면 블록" */
static 노드 *조건문_파싱(구문분석기 *파서, 노드 *조건) {
    소스위치 위치 = 조건->위치;

    /* -면 어미 소비 */
    if (확인(파서, 토큰_어미_면)) 전진(파서);

    노드 *n = 노드_생성(노드_조건문, 위치);
    n->데이터.조건문.조건 = 조건;
    n->데이터.조건문.참블록 = 블록_파싱(파서);

    /* 아니면 (else) */
    if (확인(파서, 토큰_아니면)) {
        전진(파서);
        n->데이터.조건문.거짓블록 = 블록_파싱(파서);
    }

    return n;
}

/* 반복문: "조건 -동안 블록" */
static 노드 *반복문_동안_파싱(구문분석기 *파서, 노드 *조건) {
    소스위치 위치 = 조건->위치;

    if (확인(파서, 토큰_어미_동안)) 전진(파서);

    노드 *n = 노드_생성(노드_동안반복, 위치);
    n->데이터.동안반복.조건 = 조건;
    n->데이터.동안반복.본문 = 블록_파싱(파서);
    return n;
}

/* 범위 반복: "변수가 시작부터 끝까지 반복 블록" */
static 노드 *범위반복_파싱(구문분석기 *파서, 노드 *변수, 노드 *시작값) {
    소스위치 위치 = 변수->위치;

    /* "부터" 이미 소비됨 */
    노드 *끝값 = 표현식_파싱(파서);

    /* "까지" 소비 */
    일치(파서, 토큰_조사_까지);

    /* "반복" 키워드 (선택적) */
    if (확인(파서, 토큰_식별자) && strcmp(파서->현재.텍스트, "반복") == 0) {
        전진(파서);
    }
    /* "동안" 어미 (선택적) */
    else if (확인(파서, 토큰_어미_동안)) {
        전진(파서);
    }

    노드 *n = 노드_생성(노드_범위반복, 위치);
    n->데이터.범위반복.변수이름 = 문자열_복사(변수->데이터.식별자.이름);
    n->데이터.범위반복.시작값 = 시작값;
    n->데이터.범위반복.끝값 = 끝값;
    n->데이터.범위반복.본문 = 블록_파싱(파서);

    노드_해제(변수);
    return n;
}

/* 반환문: "반환 표현식다." */
static 노드 *반환문_파싱(구문분석기 *파서) {
    소스위치 위치 = 파서->현재.위치;
    전진(파서); /* "반환" 소비 */

    노드 *n = 노드_생성(노드_반환문, 위치);

    /* 반환 값이 있는지 확인 */
    if (!확인(파서, 토큰_마침표) && !확인(파서, 토큰_어미_다) &&
        !확인(파서, 토큰_파일끝) && !확인(파서, 토큰_닫는중괄호)) {
        n->데이터.반환문.값 = 표현식_파싱(파서);
    }

    /* "이" 패턴 처리 */
    if (확인(파서, 토큰_식별자) && strcmp(파서->현재.텍스트, "이") == 0) {
        전진(파서);
    } else if (확인(파서, 토큰_조사_이)) {
        전진(파서);
    }

    어미_소비(파서);
    일치(파서, 토큰_마침표);
    return n;
}

/* 갈래문 파싱: 갈래 대상 { 경우 값: { ... } 기본: { ... } } */
static 노드 *갈래문_파싱(구문분석기 *파서) {
    소스위치 위치 = 파서->현재.위치;
    전진(파서); /* "갈래" 소비 */

    노드 *대상 = 표현식_파싱(파서);
    일치(파서, 토큰_여는중괄호);

    노드 *n = 노드_생성(노드_갈래문, 위치);
    n->데이터.갈래문.대상 = 대상;
    n->데이터.갈래문.경우목록 = NULL;

    갈래경우 *마지막 = NULL;

    while (!확인(파서, 토큰_닫는중괄호) && !확인(파서, 토큰_파일끝)) {
        갈래경우 *경우 = calloc(1, sizeof(갈래경우));

        if (확인(파서, 토큰_경우)) {
            전진(파서); /* "경우" 소비 */
            경우->값 = 표현식_파싱(파서);
            /* 콜론은 선택적 (중괄호 블록이면 생략 가능) */
            if (확인(파서, 토큰_쌍점)) 전진(파서);
        } else if (확인(파서, 토큰_기본)) {
            전진(파서); /* "기본" 소비 */
            경우->값 = NULL; /* default */
            if (확인(파서, 토큰_쌍점)) 전진(파서);
        } else {
            파서_오류(파서, "'경우' 또는 '기본'이(가) 필요합니다");
            free(경우);
            break;
        }

        /* 경우 본문: 중괄호 블록 또는 다음 경우/기본까지의 문장들 */
        if (확인(파서, 토큰_여는중괄호)) {
            전진(파서);
            int 용량 = 8;
            경우->문장목록 = malloc(sizeof(노드*) * 용량);
            경우->문장_수 = 0;
            while (!확인(파서, 토큰_닫는중괄호) && !확인(파서, 토큰_파일끝)) {
                if (경우->문장_수 >= 용량) {
                    용량 *= 2;
                    경우->문장목록 = realloc(경우->문장목록, sizeof(노드*) * 용량);
                }
                노드 *문 = 문장_파싱(파서);
                if (문) 경우->문장목록[경우->문장_수++] = 문;
            }
            일치(파서, 토큰_닫는중괄호);
        } else {
            /* 중괄호 없이 단일 문장 */
            int 용량 = 4;
            경우->문장목록 = malloc(sizeof(노드*) * 용량);
            경우->문장_수 = 0;
            while (!확인(파서, 토큰_경우) && !확인(파서, 토큰_기본) &&
                   !확인(파서, 토큰_닫는중괄호) && !확인(파서, 토큰_파일끝)) {
                if (경우->문장_수 >= 용량) {
                    용량 *= 2;
                    경우->문장목록 = realloc(경우->문장목록, sizeof(노드*) * 용량);
                }
                노드 *문 = 문장_파싱(파서);
                if (문) 경우->문장목록[경우->문장_수++] = 문;
            }
        }

        /* 링크드 리스트에 추가 */
        if (!마지막) {
            n->데이터.갈래문.경우목록 = 경우;
        } else {
            마지막->다음 = 경우;
        }
        마지막 = 경우;
    }

    일치(파서, 토큰_닫는중괄호);
    return n;
}

/*
 * 일반 문장 파싱
 *
 * 문장 종류는 시작 토큰과 표현식 뒤의 어미로 결정된다:
 * - 타입 시작 → 변수 선언
 * - 반환 → 반환문
 * - 탈출/계속 → 탈출/계속문
 * - 그 외 → 표현식 기반 문장 (호출, 대입, 조건, 반복)
 *   - 표현식 뒤 어미_면 → 조건문
 *   - 표현식 뒤 어미_동안 → 반복문
 *   - 그 외 → 표현식문 (함수 호출 등)
 */
static 노드 *문장_파싱(구문분석기 *파서) {
    /* 마침표만 있는 빈 문장 */
    if (확인(파서, 토큰_마침표)) {
        전진(파서);
        return NULL;
    }

    /* 전처리 지시문 */
    if (확인(파서, 토큰_전처리)) {
        return 전처리_파싱(파서);
    }

    /* 정적/상수 변수 선언 */
    if (확인(파서, 토큰_정적) || 확인(파서, 토큰_상수)) {
        bool 정적 = false, 상수 = false;
        while (확인(파서, 토큰_정적) || 확인(파서, 토큰_상수)) {
            if (확인(파서, 토큰_정적)) 정적 = true;
            if (확인(파서, 토큰_상수)) 상수 = true;
            전진(파서);
        }
        노드 *n = 변수선언_파싱(파서);
        if (n) { n->데이터.변수선언.정적 = 정적; n->데이터.변수선언.상수 = 상수; }
        return n;
    }

    /* 변수 선언: 타입 이름 ... */
    if (타입_시작인가(파서)) {
        return 변수선언_파싱(파서);
    }

    /* 반환문 */
    if (확인(파서, 토큰_반환)) {
        return 반환문_파싱(파서);
    }

    /* 탈출(break) */
    if (확인(파서, 토큰_탈출)) {
        소스위치 위치 = 파서->현재.위치;
        전진(파서);
        어미_소비(파서);
        일치(파서, 토큰_마침표);
        return 노드_생성(노드_탈출문, 위치);
    }

    /* 가변시작(ap, 매개변수) */
    if (확인(파서, 토큰_가변시작)) {
        소스위치 위치 = 파서->현재.위치;
        전진(파서);
        일치(파서, 토큰_여는괄호);
        노드 *n = 노드_생성(노드_가변시작, 위치);
        if (!이름_가능(파서)) {
            파서_오류(파서, "가변목록 변수 이름이 필요합니다");
            return NULL;
        }
        n->데이터.가변시작.목록이름 = 문자열_복사(파서->현재.텍스트);
        전진(파서);
        일치(파서, 토큰_쉼표);
        if (!이름_가능(파서)) {
            파서_오류(파서, "마지막 고정 매개변수 이름이 필요합니다");
            return NULL;
        }
        n->데이터.가변시작.매개변수이름 = 문자열_복사(파서->현재.텍스트);
        전진(파서);
        일치(파서, 토큰_닫는괄호);
        어미_소비(파서);
        일치(파서, 토큰_마침표);
        return n;
    }

    /* 가변끝(ap) */
    if (확인(파서, 토큰_가변끝)) {
        소스위치 위치 = 파서->현재.위치;
        전진(파서);
        일치(파서, 토큰_여는괄호);
        노드 *n = 노드_생성(노드_가변끝, 위치);
        if (!이름_가능(파서)) {
            파서_오류(파서, "가변목록 변수 이름이 필요합니다");
            return NULL;
        }
        n->데이터.가변끝.목록이름 = 문자열_복사(파서->현재.텍스트);
        전진(파서);
        일치(파서, 토큰_닫는괄호);
        어미_소비(파서);
        일치(파서, 토큰_마침표);
        return n;
    }

    /* 갈래(switch) */
    if (확인(파서, 토큰_갈래)) {
        return 갈래문_파싱(파서);
    }

    /* 반복 (C-style for loop): 반복 (초기화; 조건; 증감) { 본문 } */
    if (확인(파서, 토큰_반복)) {
        소스위치 위치 = 파서->현재.위치;
        전진(파서); /* "반복" 소비 */
        일치(파서, 토큰_여는괄호);

        노드 *n = 노드_생성(노드_반복문, 위치);

        /* 초기화 부분 (세미콜론 전까지) */
        if (확인(파서, 토큰_쌍점)) {
            /* 빈 초기화 */
            n->데이터.반복문.초기화 = NULL;
        } else if (타입_시작인가(파서)) {
            n->데이터.반복문.초기화 = 변수선언_파싱_간략(파서);
        } else {
            n->데이터.반복문.초기화 = 표현식_파싱(파서);
        }
        일치(파서, 토큰_쌍점);

        /* 조건 부분 */
        if (확인(파서, 토큰_쌍점)) {
            n->데이터.반복문.조건 = NULL;
        } else {
            n->데이터.반복문.조건 = 표현식_파싱(파서);
        }
        일치(파서, 토큰_쌍점);

        /* 증감 부분 */
        if (확인(파서, 토큰_닫는괄호)) {
            n->데이터.반복문.증감 = NULL;
        } else {
            n->데이터.반복문.증감 = 표현식_파싱(파서);
        }
        일치(파서, 토큰_닫는괄호);

        n->데이터.반복문.본문 = 블록_파싱(파서);
        return n;
    }

    /* 계속(continue) */
    if (확인(파서, 토큰_계속)) {
        소스위치 위치 = 파서->현재.위치;
        전진(파서);
        어미_소비(파서);
        일치(파서, 토큰_마침표);
        return 노드_생성(노드_계속문, 위치);
    }

    /* 반복하기 (do-while): 반복하기 { 본문 } 조건 -동안. */
    if (확인(파서, 토큰_반복하기)) {
        소스위치 위치 = 파서->현재.위치;
        전진(파서); /* "반복하기" 소비 */
        노드 *n = 노드_생성(노드_반복하기, 위치);
        n->데이터.반복하기.본문 = 블록_파싱(파서);
        n->데이터.반복하기.조건 = 표현식_파싱(파서);
        일치(파서, 토큰_어미_동안);
        일치(파서, 토큰_마침표);
        return n;
    }

    /* 이동(goto): 이동 레이블이름. */
    if (확인(파서, 토큰_이동)) {
        소스위치 위치 = 파서->현재.위치;
        전진(파서); /* "이동" 소비 */
        if (!이름_가능(파서)) {
            파서_오류(파서, "이동 뒤에 레이블 이름이 필요합니다");
            return NULL;
        }
        노드 *n = 노드_생성(노드_이동문, 위치);
        n->데이터.이동문.레이블 = 문자열_복사(파서->현재.텍스트);
        전진(파서);
        어미_소비(파서);
        일치(파서, 토큰_마침표);
        return n;
    }

    /* 레이블: 이름: 문장 (goto 대상) */
    if (이름_가능(파서)) {
        /* 미리보기: 식별자 뒤에 쌍점이 오면 레이블 */
        토큰 미리 = 미리보기(파서->분석기);
        /* 미리보기가 줄바꿈일 수 있으므로 건너뜀 */
        if (미리.종류 == 토큰_줄바꿈) {
            /* 줄바꿈 뒤는 확인 불가, 레이블 아닌 것으로 처리 */
        } else if (미리.종류 == 토큰_쌍점) {
            소스위치 위치 = 파서->현재.위치;
            노드 *n = 노드_생성(노드_레이블, 위치);
            n->데이터.레이블.이름 = 문자열_복사(파서->현재.텍스트);
            전진(파서); /* 이름 소비 */
            전진(파서); /* 쌍점 소비 */
            n->데이터.레이블.문장 = 문장_파싱(파서);
            return n;
        }
    }

    /* 표현식 기반 문장 */
    소스위치 위치 = 파서->현재.위치;
    노드 *표현 = 표현식_파싱(파서);

    /* 표현식 뒤의 어미로 문장 종류 결정 */

    /* -면 → 조건문 */
    if (확인(파서, 토큰_어미_면)) {
        return 조건문_파싱(파서, 표현);
    }

    /* -동안 → 반복문 */
    if (확인(파서, 토큰_어미_동안)) {
        return 반복문_동안_파싱(파서, 표현);
    }

    /* 대입 및 복합 대입: 식별자 = 값, 식별자 += 값, ... */
    if (확인(파서, 토큰_대입) || 확인(파서, 토큰_더하기대입) ||
        확인(파서, 토큰_빼기대입) || 확인(파서, 토큰_곱하기대입) ||
        확인(파서, 토큰_나누기대입) || 확인(파서, 토큰_나머지대입)) {
        토큰_종류 연산자 = 파서->현재.종류;
        전진(파서);
        노드 *대입 = 노드_생성(노드_대입, 위치);
        대입->데이터.대입.대상 = 표현;
        대입->데이터.대입.값 = 표현식_파싱(파서);
        대입->데이터.대입.연산자 = 연산자;

        /* "이" 패턴 처리 */
        if (확인(파서, 토큰_식별자) && strcmp(파서->현재.텍스트, "이") == 0) {
            전진(파서);
        } else if (확인(파서, 토큰_조사_이)) {
            전진(파서);
        }
        어미_소비(파서);
        일치(파서, 토큰_마침표);
        return 대입;
    }

    /* "이" 패턴 처리 (표현식문 끝의 "이다") */
    if (확인(파서, 토큰_식별자) && strcmp(파서->현재.텍스트, "이") == 0) {
        전진(파서);
    } else if (확인(파서, 토큰_조사_이)) {
        전진(파서);
    }

    /* 종결 어미 + 마침표 */
    어미_기능 기능 = 어미_소비(파서);
    일치(파서, 토큰_마침표);

    노드 *문장 = 노드_생성(노드_표현식문, 위치);
    문장->데이터.표현식문.표현식 = 표현;
    문장->데이터.표현식문.어미 = 기능;
    return 문장;
}

/* 블록 파싱: 중괄호 또는 단일 문장 */
static 노드 *블록_파싱(구문분석기 *파서) {
    소스위치 위치 = 파서->현재.위치;

    노드 *n = 노드_생성(노드_블록, 위치);
    int 용량 = 16;
    n->데이터.블록.문장목록 = (노드 **)malloc(sizeof(노드 *) * 용량);
    n->데이터.블록.문장_수 = 0;

    if (확인(파서, 토큰_여는중괄호)) {
        /* 중괄호 블록 */
        전진(파서);
        while (!확인(파서, 토큰_닫는중괄호) && !확인(파서, 토큰_파일끝)) {
            int _이전줄 = 파서->현재.위치.줄;
            int _이전열 = 파서->현재.위치.열;
            노드 *문장 = 문장_파싱(파서);
            if (파서->패닉_모드) { 동기화(파서); }
            /* 진전 감지: 파싱 후 토큰 위치가 변하지 않으면 강제 전진 */
            if (파서->현재.위치.줄 == _이전줄 && 파서->현재.위치.열 == _이전열) {
                전진(파서);
            }
            if (!문장) continue;

            if (n->데이터.블록.문장_수 >= 용량) {
                용량 *= 2;
                n->데이터.블록.문장목록 = (노드 **)realloc(
                    n->데이터.블록.문장목록, sizeof(노드 *) * 용량);
            }
            n->데이터.블록.문장목록[n->데이터.블록.문장_수++] = 문장;
        }
        기대(파서, 토큰_닫는중괄호);
    } else {
        /* 단일 문장 블록 */
        노드 *문장 = 문장_파싱(파서);
        if (문장) {
            n->데이터.블록.문장목록[n->데이터.블록.문장_수++] = 문장;
        }
    }

    return n;
}

