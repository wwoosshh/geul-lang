/*
 * 유니코드.h - UTF-8 인코딩 및 한글 유니코드 처리
 *
 * 글 컴파일러의 모든 텍스트 처리는 이 모듈을 통해 수행된다.
 * 한글 음절(U+AC00~U+D7AF)의 초성/중성/종성 분해,
 * NFC 정규화, UTF-8 인코딩/디코딩을 담당한다.
 */
#ifndef GEUL_UNICODE_H
#define GEUL_UNICODE_H

#include <stdint.h>
#include <stdbool.h>
#include <stddef.h>

/* UTF-8 코드포인트 */
typedef uint32_t 코드포인트;

/* 한글 유니코드 범위 */
#define 한글_음절_시작   0xAC00
#define 한글_음절_끝     0xD7AF
#define 한글_자모_시작   0x1100
#define 한글_자모_끝     0x11FF
#define 한글_호환자모_시작 0x3130
#define 한글_호환자모_끝  0x318F

/* 한글 음절 분해 상수 */
#define 초성_개수 19
#define 중성_개수 21
#define 종성_개수 28  /* 0 = 종성 없음 포함 */
#define 음절_개수 (초성_개수 * 중성_개수 * 종성_개수)  /* 11172 */

/* ===== UTF-8 디코딩/인코딩 ===== */

/*
 * UTF-8 바이트 시퀀스에서 코드포인트 하나를 읽는다.
 * src: 읽을 위치의 포인터
 * 반환: 디코딩된 코드포인트 (오류 시 0xFFFD)
 * src 포인터는 읽은 바이트 수만큼 전진한다.
 */
코드포인트 utf8_디코딩(const char **src);

/*
 * 코드포인트를 UTF-8 바이트로 인코딩한다.
 * cp: 인코딩할 코드포인트
 * dst: 출력 버퍼 (최소 4바이트)
 * 반환: 기록된 바이트 수 (1~4)
 */
int utf8_인코딩(코드포인트 cp, char *dst);

/*
 * UTF-8 문자열의 코드포인트 개수를 센다. (바이트 수가 아님)
 */
size_t utf8_길이(const char *s);

/*
 * 현재 바이트가 UTF-8 시퀀스의 시작 바이트인지 확인.
 */
bool utf8_시작바이트인가(char c);

/* ===== 한글 판별 함수 ===== */

/* 한글 음절(가~힣)인지 */
bool 한글_음절인가(코드포인트 cp);

/* 한글 자모(ㄱ~ㅎ, ㅏ~ㅣ)인지 */
bool 한글_자모인가(코드포인트 cp);

/* 한글 문자(음절 또는 자모)인지 */
bool 한글인가(코드포인트 cp);

/* 식별자 시작 문자로 사용 가능한지 (한글 또는 _) */
bool 식별자_시작인가(코드포인트 cp);

/* 식별자 구성 문자로 사용 가능한지 (한글, 숫자, _) */
bool 식별자_문자인가(코드포인트 cp);

/* ASCII 숫자인지 */
bool 숫자인가(코드포인트 cp);

/* 공백 문자인지 */
bool 공백인가(코드포인트 cp);

/* ===== 한글 음절 분해 ===== */

typedef struct {
    int 초성;  /* 0~18 */
    int 중성;  /* 0~20 */
    int 종성;  /* 0~27, 0=종성없음 */
} 한글_분해;

/*
 * 한글 음절 코드포인트를 초성/중성/종성으로 분해한다.
 * 한글 음절이 아니면 모두 -1을 반환한다.
 */
한글_분해 음절_분해(코드포인트 cp);

/*
 * 초성/중성/종성을 합쳐서 한글 음절 코드포인트를 만든다.
 */
코드포인트 음절_합성(int 초성, int 중성, int 종성);

/*
 * 한글 음절이 종성(받침)을 가지는지 확인한다.
 * 조사 선택(을/를, 이/가 등)에 사용된다.
 */
bool 받침_있는가(코드포인트 cp);

/* ===== NFC 정규화 (간이 버전) ===== */

/*
 * 한글 자모 시퀀스(NFD)를 음절(NFC)로 정규화한다.
 * 입력 버퍼를 제자리에서 수정한다.
 * 반환: 정규화 후 문자열 길이 (바이트)
 *
 * 주의: 이것은 완전한 유니코드 NFC가 아닌,
 * 한글 자모→음절 변환에 특화된 간이 구현이다.
 */
size_t 한글_nfc_정규화(char *buf, size_t len);

#endif /* GEUL_UNICODE_H */
