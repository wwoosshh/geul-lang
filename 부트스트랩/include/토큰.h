/*
 * 토큰.h - 글 언어 토큰 정의
 *
 * 모든 토큰 종류, 조사/어미 분류, 소스 위치 정보를 정의한다.
 */
#ifndef GEUL_TOKEN_H
#define GEUL_TOKEN_H

#include <stddef.h>
#include <stdbool.h>

/* ===== 토큰 종류 ===== */

typedef enum {
    /* 리터럴 */
    토큰_정수_리터럴,       /* 42, 0xFF, 0b1010 */
    토큰_실수_리터럴,       /* 3.14, 1.0e-5 */
    토큰_문자열_리터럴,     /* "안녕하세요" */
    토큰_문자_리터럴,       /* '가' */

    /* 식별자 */
    토큰_식별자,            /* 사용자 정의 이름 */

    /* 타입 키워드 */
    토큰_정수,              /* 정수 (int) */
    토큰_긴정수,            /* 긴정수 (long long) */
    토큰_짧은정수,          /* 짧은정수 (short) */
    토큰_작은정수,          /* 작은정수 (char as number) */
    토큰_부호없는,          /* 부호없는 (unsigned) */
    토큰_실수,              /* 실수 (double) */
    토큰_짧은실수,          /* 짧은실수 (float) */
    토큰_문자,              /* 문자 (char) */
    토큰_문자열,            /* 문자열 (char*) */
    토큰_참거짓,            /* 참거짓 (bool) */
    토큰_공허,              /* 공허 (void) */

    /* 값 키워드 */
    토큰_참,                /* 참 (true) */
    토큰_거짓,              /* 거짓 (false) */
    토큰_없음,              /* 없음 (NULL) */

    /* 구조 키워드 */
    토큰_묶음,              /* 묶음 (struct) */
    토큰_나열,              /* 나열 (enum) */
    토큰_함수,              /* 함수 표지 */
    토큰_반환,              /* 반환 (return) */
    토큰_탈출,              /* 탈출 (break) */
    토큰_계속,              /* 계속 (continue) */
    토큰_크기,              /* 크기 (sizeof) */
    토큰_포함,              /* 포함 (include) */
    토큰_정의,              /* 정의 (define) */
    토큰_아니면,            /* 아니면 (else) */
    토큰_참조,              /* 참조 (pointer marker) */
    토큰_외부,              /* 외부 (extern) */
    토큰_별칭,              /* 별칭 (typedef) */
    토큰_반복,              /* 반복 (for) */
    토큰_갈래,              /* 갈래 (switch) */
    토큰_경우,              /* 경우 (case) */
    토큰_기본,              /* 기본 (default) */
    토큰_정적,              /* 정적 (static) */
    토큰_상수,              /* 상수 (const) */
    토큰_가변목록,          /* 가변목록 (va_list) */
    토큰_가변시작,          /* 가변시작 (va_start) */
    토큰_가변끝,            /* 가변끝 (va_end) */
    토큰_가변인자,          /* 가변인자 (va_arg) */
    토큰_반복하기,          /* 반복하기 (do-while의 do) */
    토큰_이동,              /* 이동 (goto) */
    토큰_전처리,            /* # (전처리기 지시문 시작) */
    토큰_만약정의,          /* 만약정의 (#ifdef) */
    토큰_만약미정의,        /* 만약미정의 (#ifndef) */
    토큰_합침,              /* 합침 (union) */
    토큰_없으면,            /* 없으면 (null/zero check) */
    토큰_있으면,            /* 있으면 (non-null/nonzero check) */
    토큰_정리,              /* 정리 (cleanup block) */
    토큰_넘기다,            /* 넘기다 (goto cleanup) */

    /* ===== 조사 (Postpositions) ===== */
    토큰_조사_을,           /* 을 - 목적어 (받침 있는 명사 뒤) */
    토큰_조사_를,           /* 를 - 목적어 (받침 없는 명사 뒤) */
    토큰_조사_이,           /* 이 - 주어 (받침 있는 명사 뒤) */
    토큰_조사_가,           /* 가 - 주어 (받침 없는 명사 뒤) */
    토큰_조사_은,           /* 은 - 주제 (받침 있는 명사 뒤) */
    토큰_조사_는,           /* 는 - 주제 (받침 없는 명사 뒤) */
    토큰_조사_에,           /* 에 - 목적지/위치 */
    토큰_조사_로,           /* 로 - 수단/방법 (받침 없거나 ㄹ받침) */
    토큰_조사_으로,         /* 으로 - 수단/방법 (받침 있는 명사 뒤) */
    토큰_조사_에서,         /* 에서 - 출처/장소 */
    토큰_조사_부터,         /* 부터 - 시작점 */
    토큰_조사_까지,         /* 까지 - 종점 */
    토큰_조사_와,           /* 와 - 공동 (받침 없는 명사 뒤) */
    토큰_조사_과,           /* 과 - 공동 (받침 있는 명사 뒤) */
    토큰_조사_의,           /* 의 - 소유/멤버접근 */
    토큰_조사_보다,         /* 보다 - 비교 */
    토큰_조사_에게,         /* 에게 - 대상 */

    /* ===== 어미 (Verb Endings) ===== */
    토큰_어미_다,           /* -다: 서술/종결 */
    토큰_어미_면,           /* -면: 조건 (if) */
    토큰_어미_고,           /* -고: 접속/순차 (and then) */
    토큰_어미_서,           /* -서: 인과/파이프라인 */
    토큰_어미_여,           /* -여: 인과/파이프라인 (하여) */
    토큰_어미_거나,         /* -거나: 선택 (or) */
    토큰_어미_지만,         /* -지만: 양보 (but/fallback) */
    토큰_어미_도록,         /* -도록: 목적 (ensure) */
    토큰_어미_동안,         /* -동안: 지속 (while) */
    토큰_어미_때,           /* -때: 시점 (when) */
    토큰_어미_라,           /* -라: 명령 (execute) */
    토큰_어미_자,           /* -자: 제안 (spawn) */
    토큰_어미_까,           /* -까: 의문 (query/test) */
    토큰_어미_며,           /* -며: 동시 (parallel) */
    토큰_어미_면서,         /* -면서: 동시 (while doing) */

    /* ===== 구분자 ===== */
    토큰_여는괄호,          /* ( */
    토큰_닫는괄호,          /* ) */
    토큰_여는중괄호,        /* { */
    토큰_닫는중괄호,        /* } */
    토큰_여는대괄호,        /* [ */
    토큰_닫는대괄호,        /* ] */
    토큰_쉼표,              /* , */
    토큰_마침표,            /* . */
    토큰_쌍점,              /* : */
    토큰_화살표,            /* → 또는 -> */
    토큰_물음표,            /* ? */

    /* ===== 산술 연산자 ===== */
    토큰_더하기,            /* + */
    토큰_빼기,              /* - */
    토큰_곱하기,            /* * */
    토큰_나누기,            /* / */
    토큰_나머지,            /* % */

    /* ===== 비교 연산자 ===== */
    토큰_같다,              /* == */
    토큰_다르다,            /* != */
    토큰_크다,              /* > */
    토큰_작다,              /* < */
    토큰_크거나같다,        /* >= */
    토큰_작거나같다,        /* <= */

    /* ===== 논리 연산자 ===== */
    토큰_그리고,            /* && */
    토큰_또는,              /* || */
    토큰_아닌,              /* ! */

    /* ===== 비트 연산자 ===== */
    토큰_비트와,            /* & */
    토큰_비트또는,          /* | */
    토큰_비트배타,          /* ^ */
    토큰_비트아닌,          /* ~ */
    토큰_왼쪽이동,          /* << */
    토큰_오른쪽이동,        /* >> */

    /* ===== 증감 ===== */
    토큰_증가,              /* ++ */
    토큰_감소,              /* -- */

    /* ===== 대입 ===== */
    토큰_대입,              /* = */
    토큰_더하기대입,        /* += */
    토큰_빼기대입,          /* -= */
    토큰_곱하기대입,        /* *= */
    토큰_나누기대입,        /* /= */
    토큰_나머지대입,        /* %= */

    /* ===== 주소 ===== */
    토큰_주소,              /* & (주소 연산자 문맥) */

    /* ===== 특수 ===== */
    토큰_줄바꿈,            /* \n (의미있는 줄바꿈) */
    토큰_파일끝,            /* EOF */
    토큰_오류,              /* 렉서 오류 */
} 토큰_종류;

/* ===== 조사 역할 분류 ===== */

typedef enum {
    역할_없음,
    역할_목적어,    /* 을/를: 동작의 대상 */
    역할_주어,      /* 이/가: 동작의 주체 */
    역할_주제,      /* 은/는: 문맥 설정, 변수 선언 */
    역할_목적지,    /* 에: 도착점, 대입 대상 */
    역할_수단,      /* 로/으로: 방법, 타입 변환 */
    역할_출처,      /* 에서: 원본, 소스 */
    역할_시작,      /* 부터: 범위 시작 */
    역할_종점,      /* 까지: 범위 끝 */
    역할_공동,      /* 와/과: 함께, 추가 인자 */
    역할_소유,      /* 의: 멤버 접근 */
    역할_비교,      /* 보다: 비교 대상 */
    역할_대상,      /* 에게: 간접 대상 */
} 조사_역할;

/* ===== 어미 기능 분류 ===== */

typedef enum {
    어미_없음,
    어미_종결,      /* -다: 문장 끝 */
    어미_조건,      /* -면: if */
    어미_순차,      /* -고: 순서대로 */
    어미_파이프,    /* -서/-여: 결과를 다음으로 */
    어미_선택,      /* -거나: or */
    어미_양보,      /* -지만: but/fallback */
    어미_목적,      /* -도록: ensure */
    어미_지속,      /* -동안: while */
    어미_시점,      /* -때: when */
    어미_명령,      /* -라: execute */
    어미_제안,      /* -자: spawn */
    어미_의문,      /* -까: query */
    어미_동시,      /* -며/-면서: parallel */
} 어미_기능;

/* ===== 소스 위치 ===== */

typedef struct {
    const char *파일명;
    int 줄;
    int 열;
} 소스위치;

/* ===== 토큰 구조체 ===== */

typedef struct {
    토큰_종류 종류;

    /* 토큰의 텍스트 값 */
    char *텍스트;            /* 원본 텍스트 (UTF-8) */
    size_t 텍스트_길이;

    /* 리터럴 값 (종류에 따라 사용) */
    union {
        long long 정수값;
        double 실수값;
    } 값;

    /* 조사/어미일 때의 분류 정보 */
    조사_역할 역할;
    어미_기능 기능;

    /* 소스 위치 */
    소스위치 위치;
} 토큰;

/* ===== 토큰 유틸리티 ===== */

/* 토큰 종류의 한글 이름을 반환한다 (디버깅용) */
const char *토큰_이름(토큰_종류 종류);

/* 조사 토큰인지 확인 */
bool 조사_토큰인가(토큰_종류 종류);

/* 어미 토큰인지 확인 */
bool 어미_토큰인가(토큰_종류 종류);

/* 조사의 역할을 반환 */
조사_역할 조사_역할_분류(토큰_종류 종류);

/* 어미의 기능을 반환 */
어미_기능 어미_기능_분류(토큰_종류 종류);

#endif /* GEUL_TOKEN_H */
